<br><ul>July 16th, 2004 <BR>
------------------------------- <BR>
<BR>
With the update on the 14th, we changed the AA system to allow for more flexibility and control as we move forward and add additional AAs. All of the over 300 AAs were affected by this change. Some of the AAs have since been behaving incorrectly and we feel that these AA problems are enough of a hindrance to gameplay that it is necessary to correct them tonight. <BR>
<BR>
Here is the list of changes we have for you tonight; <BR>
<BR>
- AA abilities that were previously usable when using a discipline, sitting, or under the effects of stun, divine aura, or silence should once again be usable in those situations. <BR>
- Resolved an issue with Warriors Shielding that caused it to cancel instantly <BR>
- A bug in the Iksar Necro skullcap quest that allowed players to obtain it illegitimately has been fixed. <BR>
- Several tradeskilled items have had their values modified to prevent a profitability exploit. <BR>
- The key to Veeshan's Peak is now No-Drop on Firiona Vie <BR>
<BR>
<BR>
We apologize to those of you, who had raids and other activities scheduled tonight and thank you for your patience. <BR>
<BR>
Thanks all! <BR>
<BR>
<BR>
<BR>
-- The EverQuest Team <BR>
