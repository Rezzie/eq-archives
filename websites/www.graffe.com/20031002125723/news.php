** Known Issues **<br><BR>
 <br><BR>
This is the list of cosmetic issues that we're aware aren't perfect, but determined as acceptable to launch with, as none should affect peoples' ability to enjoy the game. &nbsp;<br><BR>
 <br><BR>
Rather than attempt last-minute changes that often break more than they fix, these will be addressed in an update after Lost Dungeons' launch.<br><BR>
 <br><BR>
- Paladins and Shadowknights:  The new INSPECT and OPEN ability buttons will *appear* disabled for the duration of your Harm Touch or Lay Hands timers when you use those abilities.  You may still use INSPECT and OPEN, however.  It just won't look like you can.<br><BR>
