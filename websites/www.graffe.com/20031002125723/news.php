August 26, 2003<BR>
------------------------------<BR>
<BR>
** Zone Revamps **<BR>
<BR>
- The Deep, Charasis (Howling Stones), Dragon Necropolis, Siren's Grotto, The Hole, The City of Mist, and The Accursed Temple of Cazic-Thule have been partially revamped. Gameplay in these zones has been improved, based on feedback from players and careful review of the content. The changes are not dramatic, but make these zones more entertaining to play in.<BR>
<BR>
- The Plane of Mischief, home of the trickster Bristlebane, has undergone an extensive revamp. As one of the most unusual places to visit in EverQuest, this zone is a very interesting challenge, if you have a sturdy funny bone. The improved zone is now available on Stormhammer.<BR>
<BR>
- The Greig's End revamp is now available on the Live servers. The redesign includes new encounters, items, traps, and events that make the zone a challenge for the experience group, with plenty of events for a raid group.<BR>
<BR>
<BR>
** Interface **<BR>
<BR>
- /memspellset # - Using this command memorizes one of your saved spell sets. # is the number or name (or shortened name) of the spell set you wish to memorize. Note that if you use a shortened version of the spell set name, it will match the first spell set that has those characters, so /memspellset Dots will find your "Dots and Charms", or "Dots", or "Dotsandstuff" - whichever shows up first on your list.<BR>
<BR>
- A Platinum slot has been added to the shared bank to make transactions between characters on the same account more convenient.<BR>
<BR>
- Clip Plane - The clip plane slider should work more reliably now. Where before it may not have done anything until lowered to a very low percentage, now the slider should have an effect at any setting.<BR>
<BR>
- Filters for your own pet's messages have been added. Your own pet's attacks are no longer subject to the Other Attacks/Misses filters. On the options screen there is now a separate button for filtering YOUR Pet attack messages. Also, the ability to redirect your pet attack messages to any chat window has been added to the chat window context menus.<BR>
<BR>
- /random messages can now be sent to any chat window you'd like. There is no option to filter these messages, however, to prevent any loss of the information in cases where it is needed (meaning, people can't claim that they didn't see the /random that they lost just to force a re-roll).<BR>
<BR>
- The Journal Window has been changed to make it easier to use and understand. We've merged the the two information windows into a single window.<BR>
<BR>
- Fixed a bug in the Looking for Group window. The Get Matches button should no longer grey out at inappropriate times.<BR>
<BR>
- The Bad Word Filter now filters chat channel text as well as other game text.<BR>
<BR>
- To make things a little more logical, the /inspect command will allow characters to inspect interactive objects for LDoN, and /toggleinspect allows you to toggle on or off the ability to inspect other player characters using the mouse.<BR>
<BR>
<BR>
** Alternate Advancement **<BR>
<BR>
- Purify Soul reuse time reduced from 72 minutes to 30 minutes.<BR>
<BR>
- Divine Resurrection reuse time reduced from 36 hours to 18 hours.<BR>
<BR>
- Celestial Regeneration reuse timer reduced from 72 minutes to 15 minutes. We have increased the duration from 24 seconds to 48 seconds, adjusted the amount healed, and made this a group effect.<BR>
<BR>
- Celestial Renewal duration increased from 24 seconds to 48 seconds, adjusted the amount healed, and this has become a group effect.<BR>
<BR>
- Harmtouch has been fixed. There was a problem with the timer for Harmtouch that would make it appear to reset when the character was logged out, even though it does not reset until the appropriate amount of time has passed.<BR>
<BR>
- Summon Death Charger and Summon Holy Steed have been fixed. We fixed a bug that caused these abilities to fail until the character zoned or logged out.<BR>
<BR>
- Endless Quiver no longer grants an endless supply of thrown weapons, only arrows.<BR>
<BR>
<BR>
** Spells **<BR>
<BR>
- Unswerving Hammer of Faith has had its mana cost reduced from 250 to 175.<BR>
<BR>
- Divine Barrier no longer has a damage shield component and has gained three times the healing effect.<BR>
<BR>
- Celestial Remedy has had its mana cost lowered from 190 to 150.<BR>
<BR>
- A targeting issue with pets is now fixed. There was a bug that caused pets to be untargetable with the F1 key and with pet spells that target the pet automatically. This was happening only if a pet was summoned and then a mount was summoned.<BR>
<BR>
- AE Rain Spells were resisted 100% of the time when an NPC was above level 20 and had less than 10% of its hit points left. This will no longer happen.<BR>
<BR>
- Eye of Zomm would cause a player's pet to kill itself if that pet was commanded to kill the owner's Eye. This has been corrected. Also, the Eye no longer moves at incredible speed under water, but they can now swim up and down in water.<BR>
<BR>
- PBAoE Songs no longer prevent a bard from singing if used while the bard has himself targeted.<BR>
<BR>
- Illusions can once again be cast on others while the caster is on a mount.<BR>
<BR>
- When Invisibility drops due to looting a corpse, the player will be informed that their invisibility has dropped.<BR>
<BR>
- Pets no longer break enthrall.<BR>
<BR>
- Armor of the Zealot will no longer be overwritten by Focus of Soul and Focus of the Seventh. Blessed Armor of the Risen and Ancient: High Priest's Bulwark will no longer be overwritten by Focus of Spirit or Khura's Focusing. Also, Pact of Hate and Call of Darkness should stack properly (meaning that they won't stack).<BR>
<BR>
- Illusion: Guktan was fixed on all servers a little while ago. We just wanted you to be aware that the spell will make the target look like a Guktan, not a male human.<BR>
<BR>
<BR>
** Zones and Quests **<BR>
<BR>
- Bazaar - We have increased the limit on the number of traders in the bazaar. We fixed an issue that would cause the "Begin Trader" button to be disabled if a player tried to use it when the maximum number of traders was reached. And the "Held" slot option was removed from the search, since it is not a valid option.<BR>
<BR>
- The Coirnav Encounter has been modified to respawn faster when the encounter is failed badly (if the attempt fails to reach a certain depth in the encounter).<BR>
<BR>
- The Illusion: Guktan quest had some issues. We have addressed them.<BR>
<BR>
<BR>
<BR>
** New Player Improvements **<BR>
<BR>
- After creating a new character, that character is the default selection on the character select screen.<BR>
<BR>
- At character creation, left-clicking on an attribute displays text that explains what that attribute is important for. Right-clicking allocates the maximum possible points to that attribute.<BR>
<BR>
- To help reduce clutter for the new player, the Story window no longer pops up when a player first logs in.<BR>
<BR>
- When a context Tip window pops up, if the Tip of the Day is still open, the Tip of the Day window will close. Again, this is to help reduce the clutter.<BR>
<BR>
- We've change the name of the "Camp" button in the Actions window to "Camp (Exit)" for clarity.<BR>
<BR>
- The Inventory Tip window now explains how to read books and notes, how to give/trade items with NPC's, and how to scribe spells.<BR>
<BR>
- We've added a text message on the screen to inform players when they are loading a zone, and what zone they are about to enter.<BR>
<BR>
- Added numeric background to buttons in the Hot Button window to make it easier to tell which button corresponds to which number key.<BR>
<BR>
- The Mousewheel zoom is now enabled by default.<BR>
<BR>
- New installations of EverQuest will default to 1024x768 resolution.<BR>
<BR>
<BR>
** Misc **<BR>
<BR>
- Fixed a bug that caused some equipment to be invisible on characters created recently (since our update on August 6th).<BR>
<BR>
- Luclin elemental models will now display if selected, regardless of other settings for Luclin models. You can turn these models on with no other Luclin models active, and you will see the Luclin elemental.<BR>
<BR>
- Hitting cancel on a trade window or using the /consent or /deny commands will now only send you one message, not two.<BR>
