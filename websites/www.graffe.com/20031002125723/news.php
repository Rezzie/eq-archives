<br><BR>
<br><BR>
<br><BR>
** Use Augmentations Wisely **<br><BR>
<br><BR>
If you have an item with more than one slot available and you intend to apply an Augmentation Item that can fit into any slot (i.e. Type 1), be sure to apply it last.<br><BR>
<br><BR>
For example, you have an item with three slots available and you have filled Slot 1 with an Augmentation Item.  In your bag you have an Augmentation Item that can fit into any slot on the item (i.e. Type 1) and one that fits into Slot 2.  If you use the Type 1 Augmentation Item first, it will fill the first open slot on your item, which would be Slot 2 in this case.  This means you will not be able to use the Slot 2-specific Augmentation Item you had available.&nbsp;<br><BR>
<br><BR>
So, remember, the rule is that if you have an Augmentation Item that can be used it any slot, be sure to apply it last!</p><BR><br><br>-Glinlor Stormweaver</ul></div>
