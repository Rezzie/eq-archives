Zone Revamps - Mistmoore (Legends) <BR>
----------------------- <BR>
<BR>
In Mistmoore's Catacombs, the servants of the Trueborn, a sect of vampires with Mayong Mistmoore's blood flowing through their veins, grew tired of their masters throwing them into the fray against the Norrathian intruders. They came to desire more power for themselves. <BR>
<BR>
It was not long before the first words of dissension crept through the catacombs on the will of whispers. The Trueborn servants congregated in dark, musty halls away from the eyes of their masters and over time their numbers grew along with their ambitions. Their time had come and soon they would rise up to claim their rightful place in Mistmoore Castle, gaining accolades from Lord Mayong Mistmoore himself for their brilliance and bravery -- or so they supposed. <BR>
<BR>
Over several months, the Trueborn traitors recruited an army to their cause. On a black night as the moon was swallowed by a thick veil over Faydwer, they struck, killing their masters and leading their army toward Mistmoore Castle. They slaughtered all in their path and many masters of the Scions sect, the Trueborn's rival blood-children of Mayong, were destroyed that night. <BR>
<BR>
The fierce wave of the lesser Trueborn rushed into the cellars of Mistmoore Castle without detection. Unbeknownst to all but the servants, Norrath's adventurers had uncovered winding caves beneath the surface that bypassed the castle’s defenses. In a matter of hours, the castle was overrun and the servants of the Trueborn declared themselves the true Children of Mistmoore. <BR>
<BR>
Since taking control of the castle, many Children have busied themselves repairing the damage from the battle while others continue to seek the magical ore that was uncovered in the catacombs. <BR>
<BR>
They all prepare, waiting for the day their master returns to grant them glory. <BR>
