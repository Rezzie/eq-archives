Backflagging * <BR>
----------------------------- <BR>
<BR>
As you know, when a guild gains or loses members, they often find themselves dedicating a large amount of time re-playing content that they have long since completed in order to gain access to zones for newer members. During our Community Summit this last summer, and in many subsequent discussions, we have heard the recurring desire to find a way to ease the frustrations associated with this backflagging. <BR>
<BR>
While the issue is not as simple as removing flags altogether (we feel flags are necessary for the zones they are currently attached to), we have looked into ways that we can reduce the frustrations of backflagging and feel that we have arrived at a fair and equitable solution, which is detailed below. <BR>
<BR>
- A new implementation of the 85/15 rule has been introduced into Planes of Power zones including the Plane of Time. This should resolve the outstanding problems that occur from time to time with the current 85/15 implementation. <BR>
<BR>
- The new 85/15 has also been introduced for the locked zones in Gates of Discord. <BR>
<BR>
- An alternate means of flagging guild mates for locked content in Planes of Power and Gates of Discord has been implemented that will allow you to flag raid members for the zone you are in by completing raid content within that zone. <BR>
<BR>
The implementation varies slightly from zone to zone to fit the story and nature of the zone, but in general will involve killing a raid target within that zone, looting an item, and completing a single group quest to change that item into a zone flag. This only applies to zones that don't already have a single group task or system to gain access. <BR>
