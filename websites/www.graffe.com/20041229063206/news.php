Zone Revamps - Splitpaw (Legends) <BR>
----------------------- <BR>
<BR>
Farsoth Mal, son of the Ishva Mal, leader of the Verishe Mal tribe, sat by his father’s side watching him rule his minions in Splitpaw Lair. His father may have conquered this lair some years ago, but Farsoth believed they should rule far more than just one underground kingdom. He grew to despise his father for his lack of ambition and many hours were spent in bitter arguments between the two. The Ishva Mal grew tired and angry at the ceaseless challenges to his authority, gaining as much contempt for his son as his son had for him. <BR>
<BR>
During a particularly heated argument of growls and grisly barks, the Ishva Mal reminded his willful son that his power was absolute and that he shared his throne with no one. With one quick motion, the Ishva Mal brandished his spear and sliced off his son's paw, determined that he would finally win the respect he deserved from his son. But the Ishva Mal's cruel methods would prove to be his downfall. <BR>
<BR>
The Ishva Mal grew to be a greater tyrant each day, so much so that the Verishe Mal mages and warriors secretly formed the Council of Nine to challenge his rule. Farsoth saw the plans the Verishe Mal had in store for his father, but did nothing to stop them and instead chose to use them to his advantage. <BR>
<BR>
Farsoth learned much from the council's mages and became obsessed with the summoning arts they practiced. His fascination eventually led to a plan that would secure the throne . . . for him. <BR>
<BR>
Through all he'd learned from the Verishe Mal mages, he believed he could summon a powerful entity from the Plane of Fire itself. Farsoth thought with such power at his side, nothing could stop him from ruling the Splitpaw Lair and any lands he desired. <BR>
<BR>
He prepared for weeks, researching and gathering the reagents he required and finally he set to work to summon a terrible beast to bring an end to his father's rule. <BR>
<BR>
Farsoth was distracted by his own anger, forgetting vital components as he wove the spell and the unthinkable happened. Flames shot out of the portal that Farsoth opened and, at the same time, rocks rained down upon the inhabitants of the lair. Then, the portal shifted its focus and water flooded into the corridors, extinguishing the flames and washing away the rocks. The portal shimmered once more and waves of all types of elementals poured into Splitpaw. <BR>
<BR>
Farsoth certainly attained part of his goal. His father was killed, but so to were most of the gnolls he wished to lead to greatness. <BR>
<BR>
Now he is a prisoner to the swarms of enraged elementals that have made Splitpaw Lair their new home. <BR>
