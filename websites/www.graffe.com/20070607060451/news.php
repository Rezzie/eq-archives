<br><ul>May 16, 2007<BR>
_____________________<BR>
<BR>
*** Highlights ***<BR>
<BR>
- Jardel's Hook Mission: Long ago, a holy war brewed between the Redfeather aviaks and the Suncrest goblins. Using a blessed artifact of the ocean god, Prexus, the Suncrest goblins shaped the tide in the Buried Sea. The aviaks, fearful of the power the goblins possessed countered with their own artifact blessed by Karana, the god of storms, which created monsoons and heavy rain. This era in the Buried Sea became known as the Time of Storms. Many years after it began, the war ended abruptly when a powerful siren stole both artifacts and hid them somewhere in the vast expanse of the Buried Sea. Find out what happened to these powerful artifacts in a new mission in the Buried Sea! <BR>
<BR>
- Ocean of Tears: The Ocean of Tears has a new look. Sail over to check it out and to experience the return of the boats!<BR>
<BR>
- A new integrated web browser has been added to EverQuest. This web browser will be accessible from either the help menu or the /petition command. From this point forward, all problems related to switching to an external browser will be eliminated because this browser will pull up in an in-game window.<BR>
<BR>
- Congratulations to the winners of EverQuest's "The Fabled Adventurer" Video Contest. The First Place winner was Quarken of the Maelin Starpyre server. You can view the winning video on EQPlayers.station.sony.com.<BR>
<BR>
<BR>
*** Recently Reported Issues ***<BR>
<BR>
First of all, we want to thank everyone for their assistance with reporting bugs in the game. Your effort helps us pinpoint the source of bugs so that they can be resolved as quickly as possible. Our main source of reports on bugs comes from the in-game submitted /bug reports but we also get verification from /petitions and forum posts. <BR>
<BR>
In order to make sure everyone knows what we are currently aware of and working on, we will be frequently posting a List of Recently Reported Issues that have been addressed. The following is a list of the Recently Reported issues that have been resolved in this update:<BR>
<BR>
Issue: Tartons Wheel (A Rod)<BR>
Summary: Players are unable to combine Staff of the Wheel in A Glowing Chest. The recipe that players must combine in order to get the Staff of the Wheel and Star of Eyes, eventually leading to the Ice Comet spell, is currently not useable. <BR>
Status: The combine works properly again.<BR>
<BR>
Issue: Elder Longshadow #4 Trailing Longshadow.<BR>
Summary: Players can port into the instance before completing the first element of the quest. The player is ported into the instance with no way to update element 0 of the task.<BR>
Status: You can no longer enter the instance before you have updated the first element of the quest.<BR>
<BR>
Issue: Shaman spell 'Protection of Wishka' when scribed becomes 'Wishka's Favor'.<BR>
Summary: Name and description of item do not match the spell that is scribed from.<BR>
Status: The spell scrolls have been changed to the proper spell name.<BR>
<BR>
Issue: NPC 'a goblin headmaster' in the 'Ocean of Tears' zone is not spawning.<BR>
Summary: The Goblin Headmaster is no longer spawning in the Oot zone.<BR>
Status: The Goblin Headmaster is spawning again in the new version of Ocean of Tears. <BR>
<BR>
Issue: EQPlayers site not updating character profiles.<BR>
Summary: People are reporting that their character profiles are still not updating.<BR>
Status: With this update, work will be done on the EQPlayers database. The hardware will be upgraded. We believe that this will help prevent the database from becoming overloaded which is what was causing many people not to get updated. There are some other changes that they will be making as well in regards to the flow of information. Once the upgrade is done and the changes are made we will be able to track the situation closer to determine exactly what is causing the problem if it occurs again.<BR>
<BR>
Issue: Vah Shir Illusion makes Rogues naked.<BR>
Summary: Armor doesn't show up correctly when under illusion.<BR>
Status: The illusion should no longer hide the armor.<BR>
<BR>
<BR>
*** Items ***<BR>
<BR>
- Guardian Blade is no longer useable by Paladins, Shadowknights, or Rangers as they can't actually obtain it.<BR>
- Improved Silver-Wrapped Turquoise Earring.<BR>
- Changed the icon for Ink of Terris Thule.<BR>
- The Bucket of Slop used in the Meek Shall What? task is now Lore.<BR>
- Fixed backstab issues with Eye of the Onyx Guardian and Rogue Epic 2.5.<BR>
- Added a worn effect to Venesh's Evergreen Gauntlets.<BR>
- Improved Jagged Blood, Iron Ring, Scorched Froststeel Ring, and Ice Enameled Gorget of Battle.<BR>
- TBS pet class items are now useable by Frogloks.<BR>
- Added Pelzia's Replenishment to Keikolin's Forcephase Sandals.<BR>
<BR>
<BR>
*** Quests & Events ***<BR>
<BR>
- Fixed a bug with Bloodeye's event. He will now cast his curse less often.<BR>
<BR>
<BR>
*** Spells ***<BR>
<BR>
- Dawn's Sunlight now works on Burst of Sunlight Rk. II.<BR>
- Mana Flare and Mana Recursion have been changed so that DoT spells can now also trigger the damage effect.<BR>
- Reduced the reuse timer on the Unholy Aura Shadow Knight Discipline to 36 minutes.<BR>
<BR>
<BR>
*** NPC ***<BR>
<BR>
- Caerlyna has decided she is willing to hold brown handmade backpacks.<BR>
- Temporarily deactivated the "Avalanche" event in Icefall for performance reasons.<BR>
<BR>
<BR>
*** Tradeskills ***<BR>
<BR>
- Changed the recipe of the Yttrium-Imbued Oak Bow to return the Planing Tool.<BR>
- Altered the recipe "Summon Heatstone" to use Breath of Solusek instead of "Summon Element Scroll."<BR>
- Added recipes for Spell: Summoning: Earth and Spell: Conjuration: Fire.<BR>
<BR>
<BR>
*** Missions ***<BR>
<BR>
- There's an exciting new quest and mission series in the Barren Coast. Speak to Farwhendle Higglespout in The Barren Coast for more details.<BR>
<BR>
<BR>
*** Tasks ***<BR>
<BR>
- Changed the cultural tasks to be available at the required level of the items they make not the recommended level.<BR>
- In shared tasks if the leader disconnects, the leadership is passed to another active member of the shared task.<BR>
<BR>
<BR>
*** AA ***<BR>
<BR>
- All Shadow Steps from the Wizard AA "Dimensional Instability" should now have the same range. <BR>
- The Silent Casting AA ability will now appear in the "Short Buff" window. <BR>
- The AA Glyph of Destruction will now increase the damage of all Damage over Time spells when active.<BR>
<BR>
<BR>
*** Miscellaneous ***<BR>
<BR>
- Armor of Experience should now display the correct absorption messages.<BR>
- Mipmapping has been setup so that it is turned in every model when the mipmapping option has been selected. Mipmapping allows for a clearer view of the models as distances change.<BR>
- When you cast a Heal Over Time spell on another player you can see how many hit points you've healed that player. This can be filtered using the Heal Over Time filter in your Options Window under Filters.<BR>
- The Tribute Benefits window is now closed whenever the player changes to or from shroud character form.<BR>
- Fixed a message error with throwing weapons.<BR>
- Corrected an inconsistency with the purchase messages of player merchants.<BR>
- Corrected an issue with the /corpsedrag command.<BR>
- Added two new commands /booklearn and /bookunlearn that take a single argument (the item # to be learned/unlearned.)<BR>
- Characters with non-weapons equipped should not longer get error messages when they autoattack.<BR>
<BR>
<BR>
*** User Interface Changes ***<BR>
<BR>
- Changed -<BR>
<BR>
EQUI.xml<BR>
SIDL.xml<BR>
EQUI_NoteWindow.xml<BR>
EQUI_BookWindow.xml<BR>
<BR>
- New -<BR>
<BR>
EQUI_HtmlWnd.xml<BR>
<BR>
<BR>
*** Previously Updated ***<BR>
<BR>
- Increased the experience gains in the Dragon Necropolis and Wall of Slaughter Hot Zones slightly.<BR>
<BR>
<BR>
*** Future Tradeskill Changes ***<BR>
<BR>
We will be making a change to the cultural armor that will nullify the usage of all existing drops in the cultural armor and symbol crafting process. For the slot 12 raid augments, only the drops for the seals will be changing, the rest of the drops required for the various steps of the slot 12 raid augments will be staying the same. Those of you who have stockpiles will want to make use of them as best and quickly as possible, as once the changes go in, the currently existing cultural recipes will no longer work.<BR>
<BR>
For more detailed information please see the "Upcoming Cultural Changes" sticky thread in the Artisans Way forum of the official EverQuest forums.<BR>
<BR>
