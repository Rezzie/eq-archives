<BR>
*** Highlights ***<BR>
<BR>
- Have you ever been to an SOE Fan Faire? Have you ever been to Las Vegas? Now's the time to think about doing both at the same time! August 2nd - 5th SOE is holding its 2007 Fan Faire at the Rio All-Suite Hotel and Casino in Las Vegas Nevada! Visit the official SOE Fan Faire website at http://events.station.sony.com/fanfaire/ for more information about other activities. <BR>
<BR>
- So you want to see more events? Ever try running your own? Visit the EQplayers website to view the Tip of the Week on running Player-Run Events in EverQuest.<BR>
<BR>
<BR>
<BR>
*** Spells ***<BR>
<BR>
- The mana cost incurred by fizzling now includes focus effect and specialization savings, reducing the amount of mana that is used on a fizzle.<BR>
- The bard fizzle cap has been increased from 95% to 100%.<BR>
- The bard command '/melody' has been changed to now attempt to re-sing a song that misses a note, resuming the melody chain once it's successfully done so.<BR>
- Specialized bard songs (mez, charm, stun, etc.) will now have a 2% fizzle rate, given max skill and level-appropriate stats.<BR>
- General purpose bard songs (regen, haste, speed, etc.) will now have a 0% fizzle rate given max skill and level-appropriate stats.<BR>
<BR>
- The EverQuest Team<BR>
