<div id="main208" style="display:none;">
<br><ul>June 11, 2003 3:00 am <BR>
------------------------------ <BR>
** Experience System Change - Grouping ** <BR>
<BR>
We've made some improvements to the way experience is rewarded in EverQuest. These improvements are aimed at positively reinforcing the act of grouping. After all, this is a Massively Multiplayer game. <BR>
<BR>
Prior to this update groups gained a 2% to 20% experience bonus for having two to six members. <BR>
<BR>
As of today this bonus has increased to range from 20% to 80% for having two to five members. When a group adds a sixth member, the 80% bonus remains, but the experience gained is only divided by 5 before being distributed. The sixth group member no longer causes the experience gain to be divided by 6. <BR>
<BR>
Planes of Power zones had an increased experience award above and beyond those of most other zones. Because of these new dramatic improvements to group experience gains, we will be reducing the zone-specific experience bonus for Planes of Power zones a bit. As long as character is grouped with at least one other in these zones, they will see an improvement in experience gain over the old system. The one down side to the new system is that there will be a small decrease in experience for those who do not group, and only for those who do so in Planes of Power zones. <BR>
<BR>
<BR>
** Experience System Change - Level 60+ ** <BR>
<BR>
In addition to the above changes, we have increased the range of NPC levels that give a character experience after they reach level 60. This will open up many of the "old world" zones to players previously restricted to Planes of Power zones for experience gain. The range of NPCs that will be "blue" to a level 65 character has been increased as far as level 50, with the "light blue" range extending to level 45. The best experience gains, however, are still for fighting things around and above your level. This should open up areas for high level characters in groups and solo. <BR>
<BR>
<BR>
** Interface ** <BR>
<BR>
- Shared Bank Vault - We've added two shared bank slots to the interface. These extra bank slots can be accessed by any character on the same account and server. No-Drop and No Rent items can't be placed in these bank slots, and only one of any specific Lore item can exist in the shared bank at any time. <BR>
<BR>
- Spell Context Menus - This is a new interface menu that will make memorizing spells less tedious. To access the spell menu, just right-click an empty spell gem. This will open a list of spell categories. Point your mouse at a category and a list of spells will be displayed. Select a spell from that list to memorize it in that slot. Your character will sit down, memorize the spell and stand back up. <BR>
<BR>
- Spell Favorites - Spell favorites are an easy way to memorize a full set of eight spells without the tedious book searching. Right-clicking on the spellbook icon in your spell window will open up the spell favorites menu. You will be able to save your current spell lineup as a favorite, delete a favorite or memorize all the spells in one of your already saved spell sets. You can save as many as ten sets of spell favorites. As with the new spell context menus mentioned above, this will not replace the spellbook, it is meant to make memorizing large numbers of spells more convenient. <BR>
<BR>
- Spell Descriptions - We have expanded the spell description window. Along with the information that has always been available, we have added an explanation about what the spell does in general that includes damage and duration when appropriate. You can see these new descriptions by right-clicking and holding on the spell scroll, the spell gem in your Spells window, or the spell icon in your spellbook. <BR>
<BR>
- New Camera - We've added a new camera mode to the game. Lovingly dubbed the EQ Cam, it uses the mouse wheel to allow you to zoom away from your character into a "chase" camera. This only works from first person mode (the default view). Using the mouse wheel you can determine the distance of the camera from your character, or to zoom back into first person mode. When in the chase mode, mouselook turns the character and camera together and you can change the pitch of the camera using the mouse. <BR>
<BR>
- NPC Names - Many NPCs will be displaying additional titles to make it clearer what their job is. For example, an NPC that sells tailoring supplies may have the title [Tailor Supplies] under their name. This will help folks find the proper merchant. It's analogous to them hanging out their shingle. <BR>
<BR>
- Rare Character Select Crash - Using the arrow keys to try to change the selected character at the character select screen when there were no characters on the account was causing people to crash out of the game. That's fixed now. <BR>
<BR>
- Guild Message of the Day - Using certain HTML codes when setting up a Guild Message of the Day was causing all members of the guild to crash. While this may have been amusing for pranks on your guild, we still decided to fix it. <BR>
<BR>
<BR>
** Spells ** <BR>
<BR>
- Spell level limits now enforced - Some high level spells cannot be cast on characters under a certain level. In the past a caster could get around this level restriction when casting a group version of a spell by targeting a higher level group member, and the spell would take effect on the whole group, regardless of their level. That loophole has been closed. We're sorry if you have grown accustomed to the system working this way, but those level restrictions are important to the integrity of lower level gameplay. <BR>
<BR>
- Incineration and Conglaciation of Bones - Both of these spells should now crit and stack (when cast by different casters). <BR>
<BR>
- Divine Intervention - This spell should now be reporting the amount of healing it delivers. <BR>
<BR>
- Healing Messages - The target of a healing spell will now get a message telling them the caster's name and the amount of healing they receive. <BR>
<BR>
- Reporting Damage Over Time - Damage over Time (DoT) done to NPCs will now be reported to the caster every time it does damage. These messages can be filtered in the Options window. <BR>
<BR>
- Boon of the Garou - The level restriction for this spell has been removed. We have also fixed a bug with the spell that would prevent it from being cast on target that are not on a horse if the enchanter is on horseback (spell still cannot affect those on horseback). <BR>
<BR>
- Wake of Karana - To improve the usefulness of this spell, we have lowered the recast time from 90 seconds to 15 seconds. <BR>
<BR>
- Beastlord Warders - Beastlords will once again be able to summon their warders in the Plane of Sky. Sorry for the delay in getting this one fixed properly. <BR>
<BR>
- Familiars - Familiars will no longer attempt to equip items. This was causing them to try to wear weapons inside their heads, which probably isn't a good thing for them to do. <BR>
<BR>
- Spirit spell line - We have added +75 dexterity to the effects of Spirit of Flame, Spirit of Rellic, and Spirit of Snow. <BR>
<BR>
- Spell stacking and blocking - Changed the following spell lines to cause them to bounce off of each other. Each line should block everything from the other line, but still be overwritten by spells of the same line. <BR>
<BR>
Spiritual Strength <BR>
Spiritual Brawn <BR>
Spiritual Vigor <BR>
<BR>
Strength of Tunare <BR>
Strength of Nature <BR>
<BR>
Brell's Mountainous Barrier <BR>
Brell's Stalwart Shield <BR>
Brell's Steadfast Aegis <BR>
<BR>
- Translocation spells - Fixed a bug with translocation spells. If a second translocation spell was cast on a character before he had accepted the first, both would fail. <BR>
<BR>
- Spell casting pets - In the past, pets were restricted to casting spells of level 20 and below. We have given them access to higher level spells. You may want to be careful with them until you get used to their new abilities. <BR>
<BR>
- Drunken Riders - We fixed an odd bug that allowed people who were extremely drunk to cast spells without mana cost while on a horse. Yes, crazy bugs like that actually happen. <BR>
<BR>
<BR>
** Zones ** <BR>
<BR>
- Droga and Nurga - The mines of Nurga have been inhabited by crafty Goblins and stolid Burynai for many long years, digging and slaving away under the earth. It's time to shake them up! The redesign for these zones are now on the Legends server, Stormhammer. The mines of Nurga are a fun zone for characters of levels about 40 to 55. The Temple of Droga accommodates characters around 50th to 65th level. Both zones will have some new events and quest, while retaining the distinct feel of the zones. <BR>
<BR>
- Veksar - Since the rediscovery of the lands of Kunark, there have been rumors springing from the ancient lore of the Iksar. Most of those rumors remain unfounded, as gaining access to the lore of the insular Iksar is no easy task. One of the most persistent legends is about a city that lies buried under the Lake of Ill Omen. That city was called Veksar, and it is up to you to discover the tale of its fate. <BR>
<BR>
- Planes of Power access - Planes of Power has provided us with valuable insight into the group and guild dynamics that are involved in large scale games. The way that we had expected guilds to advance through the story of Planes of Power has become a problem for some guilds as some members have fallen behind and found it difficult to catch up. Obviously this was not our intention. We want people to play with their friends and guild mates. <BR>
<BR>
To correct this issue, we will be allowing characters to enter the following Planes of Power zones by meeting a simple level requirement: <BR>
<BR>
Level 55 and over <BR>
Plane of Storms <BR>
Plane of Torment <BR>
Plane of Valor <BR>
Crypt of Decay <BR>
<BR>
Level 62 and over <BR>
Bastion of Thunder <BR>
Halls of Honor <BR>
<BR>
Characters that don't meet the level requirement may still enter these zones using the existing flags, and raids may still bring additional unflagged or lower level characters with them. All other planes with flag or story requirements for entry will still require those flags, and those flags can still be obtained as they always have. <BR>
<BR>
This will make the majority of Planes of Power zones available to characters over the minimum level, and will allow their guilds to travel and adventure together in the planes. We understand that those that have already earned the entry flags for these zones have put some effort into that accomplishment. We will be giving a reward to those that have done so, though you'll have to wait to see what that reward is. <BR>
<BR>
If you have completed the requirements for flags for these zones, you may wish to visit Gram Dunnar. <BR>
<BR>
- Great Divide - The Plane of Knowledge portal in the Great Divide will now be available when the Ring War is in progress. <BR>
<BR>
- Bastion of Thunder - We have upgraded the loot available from many of the mini-bosses in the Bastion of Thunder to make the reward more inline with the risk. <BR>
<BR>
- Sol Ro's Tower - They look like frogs, hop like frogs and now they even react to spells like frogs. Flame Frogs are now flagged as animals. <BR>
<BR>
- Rallos Zek encounter - Rallos Zek will remove anyone from the zone that attacks him if they are not part of the raid that has him engaged. Even in war, there are rules to be followed. <BR>
<BR>
<BR>
** Items ** <BR>
<BR>
- Tradeskill item flag - There is a new flag on items. When viewing the information about an item, if you see that it has a Tradeskill flag, it is an item that can be used in a tradeskill recipe. This isn't to clearly spell out every item that is used in every tradeskill recipe. The intent is to assist those who are finding items that do not already have an obvious use, by letting them know that the item is used in a tradeskill recipe, somewhere. <BR>
<BR>
- LoY "Fine Steel" - Many of the more commonly dropped weapons in the Legacy of Ykesha zones were a bit too powerful for the ease and level needed to get them. Some of the most out of line items were the Harpoon, Luggald Trident, and Luggald Dagger. We've decided that it is necessary to tune them down a bit. <BR>
<BR>
- The Hollow Acrylia Obelisk - This is the old Acrylia key. It has been changed into a charm. This charm can be used by anyone, not just those that own LoY. <BR>
<BR>
- Bows - The Longbow of the Grimlings and the Bow of Gloaming have both been given ranges. They can now be used properly. <BR>
<BR>
- The Burning Flame Shield - This item had the wrong resist modifiers. It now has a +30 fire resistance and a -10 cold resistance. <BR>
<BR>
- Bazaar Search - Non-instrument items with instrument modifiers will now show up in instrument type searches in the Bazaar. <BR>
<BR>
- Ornate Stone Medicine Totem - This item can now be equipped. <BR>
<BR>
- Storm Reaper Thorn Machete - This newbie Halfling quest weapon is now flagged as magical. <BR>
<BR>
- Charm of the Brotherhood - This item will now give a bonus when grouped with others of either the same class or race. <BR>
<BR>
- Dagger of Distraction - This item can no longer be used by Monks. <BR>
<BR>
- Edge of Eternity - Beastlords and monks can no longer use this item. <BR>
<BR>
- Veil of Lost Hopes and Orb of Clinging Death - The casting times on both of these items has been lowered. <BR>
<BR>
- Magi`Kot's Pants of Convergence - The effect on this item has been changed to summon a Rod of Mystical Transvergance. <BR>
<BR>
- Cloak of the Falling Skies - The focus effect this item has changed to Preservation of Solusek <BR>
<BR>
- The Wristguard of Keen Vision - This item is now useable by int. casters. <BR>
<BR>
- The Mask of Conceptual Energy - This item is now useable by priests. <BR>
<BR>
<BR>
** Alternate Advancement ** <BR>
<BR>
- Lifeburn - This ability would cause an undue amount of aggro to be generated because it would bring the caster to an extremely low number of hit points, making them look like tasty targets to enemies nearby. To lessen this problem, Lifeburn will no longer reduce the caster to 10 hit points, but to about one fourth of the caster's hit points. This should prevent the additional agro generated while retaining the risk for using the ability. <BR>
<BR>
- Feigned Minion - The Child of Bertoxxulous pet will no longer cast buff spells to break his feign when under the Feigned Minion effect. <BR>
<BR>
<BR>
** Quests and Tradeskills ** <BR>
<BR>
- Bankers - There were a few quests that required giving an item to a banker. When we recently removed the ability for characters to give items to bankers to help prevent mistakenly losing items, those quests became unavailable. We have moved those quests to new NPCs. If you see any other quests that ask you to give something to a banker, please use /bug and report it to us. <BR>
<BR>
- Newly Stackable Items - Valor Crystals, Ceramic Linings, Holgresh Fur and Casks are now stackable. Also, Valor Crystals can be sold to vendors. <BR>
<BR>
- Sewer Catfish - These unappetizing fish have begun to appear in Cabilis. <BR>
<BR>
- Klok Foob - Will now accept a Catfish Croak Sandwich. <BR>
<BR>
<BR>
** Changes for New Characters ** <BR>
<BR>
- Newbie Starting Locations - We've modified all locations where new characters start to make them a bit more convenient for the new player. They should all start very near their guild masters. <BR>
<BR>
- Nudging - New characters will be given a text message to encourage them to hand in their newbie note to their guild master. For new players, this is one of the key things to get them involved in gameplay, and one of the easiest to overlook. <BR>
<BR>
- Guild Masters - To help guide new characters, Guild Masters now take a more active role in helping out new recruits by directing them to some starting points for quests. <BR>
<BR>
- Quests - There are even more quests in the newbie zones, most of them revolving around the pests that seem to inhabit even the grandest cities. <BR>
<BR>
<BR>
** Miscellaneous ** <BR>
<BR>
- We are working on an update to the Luclin player character model animations. This will not be a complete revamp of all the animations, but will address some of the most common concerns. However, in the mean time you may see some unusual animations, particularly when shorter PCs are running. <BR>
<BR>
<BR>
** UI File Changes ** <BR>
<BR>
