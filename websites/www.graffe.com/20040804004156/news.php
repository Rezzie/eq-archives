</td><td class=menuhead nowrap>July 14th, 2004</td></tr><tr><td class=menudata colspan=2>
<div id="main201" style="display:none;">
<br><ul>7/14/2004 <BR>
<BR>
Headlines! <BR>
------------------- <BR>
Veeshan's Peak has been re-worked. Enjoy the new challenge. <BR>
A new Adventure Zone appears on Stormhammer. <BR>
You can now remove augments without losing them. <BR>
<BR>
Veeshan's Peak! <BR>
------------------- <BR>
With this revamp we have Updated the level and NPC list for the zone: Please note that this zone is now an elemental level zone! The entry quest to Veeshan’’s Peak has been changed. The original key quest has not been modified. However, the new entry quest uses the old key and offers an alternate quest to do instead of the original key quest. <BR>
<BR>
- Items in the zone are now of elemental planes quality. <BR>
- Faction now matters: Dragons in Veeshan’s Peak will now speak to players if you have the correct faction and can understand them. <BR>
- New quests and lore: The dragons in Veeshan’s Peak will now give players the option to acquire items through questing for them. <BR>
- The zone is now an exp zone as well: Hunting in the areas before the dragons yields experience and loot. <BR>
<BR>
<BR>
New Adventure Zone for Stormhammer <BR>
------------------- <BR>
Stormhammer gains a new adventure zone. This is a 2 group adventure zone for characters of level 65. <BR>
<BR>
Can you help the Eldritch Collective find and rescue Professor Bartleby Beckelbracker? Search the Desert of Ro for the whereabouts of Bartleby and his assistant Bomo and try to save them from the steel clutches of their enemies. <BR>
<BR>
<BR>
Augment Removal * <BR>
------------------- <BR>
- You can now purchase special "distillers" that will remove augmentations without destroying them. These distillers are sold by a merchant in Butcherblock. The item inspect window for an augmentation will tell you which distiller is required to remove it. <BR>
<BR>
- The existing solvents can still be used to destroy an augmentation if you don't want to keep it. You no longer need a different solvent for each augmentation slot. The interface will allow you to select which augmentation you wish to delete. <BR>
<BR>
- For those with custom UI's, you will need to update your EQ_Container.xml file in order to use the new augment window. <BR>
<BR>
<BR>
Pets * <BR>
------------------- <BR>
- Zoning pets are in. Persistent pet data includes: Items, Spell Effects, hit points, mana, name, height, and mana cost to summon them. All PC summoned pets should zone with you in all instances except for when you die. Pets will also follow the same rules as no rent items if you log with a pet. <BR>
<BR>
- Pets can be shrunk again. Please wash them in only Cold water to prevent inadvertently reducing their size. <BR>
<BR>
<BR>
Tribute Changes * <BR>
------------------- <BR>
- Some items have had their favor value lowered. <BR>
<BR>
- Players will now "Pre-pay" for tribute benefits. When benefits are activated, the player will immediately be charged for those benefits from their Favor pool. After that, the player will be charged every 10 minutes for the next 10 minutes of benefit. If benefits are deactivated before the timer is depleted, no refund is given for the time remaining. <BR>
<BR>
- Players now have the ability to activate/deactivate the tribute benefits they've selected (as a whole) whenever they like using a button on the Tribute Benefit Window. However, every time the player activates his benefits, he will be "Pre-pay" (see above). The ability to change selected benefits (downgrade) away from the Tribute Master has been removed. <BR>
<BR>
- When visiting a Tribute Master, the player's benefits will deactivate. Players may re-organize their selected benefit load, but will not be able to "preview" the benefits (seeing the effect on their character when the benefit is selected). Benefit descriptions should give the player a good indication of what the benefit will do when activated. <BR>
<BR>
- The EQ Label that shows the Tribute Benefit Timer will now show green when benefits are active and gray when they are not. <BR>
<BR>
<BR>
Item Changes <BR>
------------------- <BR>
- Manastones will no longer work in Forgotten Halls. It was not intended for the item to be used there and some naughty folks were taking advantage of the zone. <BR>
<BR>
- Malcohm Thimbleton, in the Plane of Knowledge will now swap out some Robe Graphic items for Tunic Graphics. * <BR>
<BR>
- The Staff of Revealed Secrets now has a 2hb weapon model instead of the little stick it had before. Enjoy your bigger stick. <BR>
<BR>
<BR>
UI Changes <BR>
------------------- <BR>
- It is now possible to display your mana as a numeric value on your inventory screen. * <BR>
- By mousing over your buff icons, you will now be able to see the buffs remaining duration. * <BR>
<BR>
Berserkers <BR>
------------------- <BR>
- Berserkers now have innate crits beginning at level 12. <BR>
- Tainted Axe of Hatred now comes in stacks of 20. <BR>
<BR>
<BR>
Additional Notes <BR>
------------------- <BR>
- You now have the ability to sell back LDoN items and augments to the adventure merchants for a percentage of their original cost. * <BR>
<BR>
- We have increased the number of NPC debuff slots from 30 to 50. Necromancers rejoice. * <BR>
<BR>
- The Doppleganger AA's re-use timer has been reduced to 30 minutes. * <BR>
<BR>
- Sprint and Planeswalk have had their movement rate increased. * <BR>
<BR>
- Beware! Many named mobs in Gates will now summon. <BR>
<BR>
- The Hide all traders option, for the Bazaar, will no longer need to be reset each time you enter the zone. * <BR>
<BR>
- /stopspellcast will no longer give spells recast delays. <BR>
<BR>
- Leadership: Group/Raid mark npc should now update for people that are added to the group or raid. <BR>
<BR>
- Main assist will no longer clear if the main assist zones. <BR>
<BR>
- Bards should once again play the stringed instrument animation when playing a stringed instrument song. <BR>
<BR>
- Hynid fleshrippers, tide feasters, and shore stonemites in Natimbi no longer hit so hard. <BR>
<BR>
<BR>
<BR>
<BR>
-- The EverQuest Team <BR>
<BR>
*Denotes a change requested by the community <BR>
<BR>
<BR>
