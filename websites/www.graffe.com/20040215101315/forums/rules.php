<p align="center"><font color="#CCCCCC" size="5"><strong><font face="Arial">General 
  Forum and Posting Rules</font></strong></font></p>
<hr align="center" width="75%" size="1">
<font size="5"><strong><font face="Arial"> </font></strong></font> 
<ul>
  <li><font size="2" face="Arial"><strong>No posting confidential materials. </strong> 
    </font> 
    <ul>
      <li><font size="2" face="Arial">Posting any type of non-public documentation 
        including, but not limited to, non-discloser agreements and private Sony 
        employee contact information, is forbidden.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>No advertisements. </strong> </font> 
    <ul>
      <li><font size="2" face="Arial">The following are not allowed and are considered 
        advertisements… </font> 
        <ul>
          <li><font size="2" face="Arial">Outwars / Outwars-clone link spamming 
            / hit farming.</font></li>
          <li><font size="2" face="Arial">Guild recruiting.</font></li>
          <li><font size="2" face="Arial">Link spamming.</font></li>
        </ul>
      </li>
      <li><font size="2" face="Arial">Posters that violate these rules will have 
        their posts edited / deleted and have either a warning issued or face 
        suspensions / banning.  The action taken will be decided on a case-by-case 
        basis.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>No pornography. </strong> </font> 
    <ul>
      <li><font size="2" face="Arial">This includes but may not be limited to 
        pornography, sexually explicit pictures, writing, or other material whose 
        primary purpose is to cause sexual arousal. This covers ASCII porn, /cyber 
        logs, and links to pornography.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>Personal attacks are not allowed. </strong> 
    </font> 
    <ul>
      <li><font size="2" face="Arial">Debate the post, not the poster.  Personal 
        attacks are taken seriously and will be dealt with using whatever action 
        is needed.  If you think you are being attacked you should ask yourself 
        why it is an attack.  If it simply disagrees with your post and tells 
        you that you are wrong, this is not an attack.</font></li>
      <li><font size="2" face="Arial">Violent personal attacks and threats will 
        result in a suspension or permanent banning, depending on the situation. 
        </font> 
        <ul>
          <li><font size="2" face="Arial">&quot;Your post is quite idiotic.&quot; 
            - Not a personal attack.</font></li>
          <li><font size="2" face="Arial">&quot;You are an idiot.&quot; - Mild 
            personal attack, <strong>generally</strong> not a problem.</font></li>
          <li><font size="2" face="Arial">&quot;You are a f##king idiot... I wish 
            death on you and a pox on your family!&quot; - Violent personal attack. 
            Big proble </font></li>
        </ul>
      </li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>Post in the correct forums. </strong> 
    </font> 
    <ul>
      <li><font size="2" face="Arial"><span
style='font-family:"Courier New"'><span style='font:7.0pt "Times New Roman"'>&nbsp; 
        </span></span>EQ related threads belong in the EQ forums.  Non-EQ related 
        threads belong in the OT forum.  Rants belong in the rant forum. If you're 
        not sure it's a rant, chances are it is and you should post there.</font>
        <ul>
          <li><font size="2" face="Arial">&quot;I don't like how this ability 
            works.&quot; - Not a rant.</font></li>
          <li><font size="2" face="Arial">&quot;In general, this spell is really 
            stupid.&quot; - Semi-rant, acceptable in general.</font></li>
          <li><font size="2" face="Arial">&quot;I saw a mother f##king dr00d quad-kiting 
            and his spells looked cooler, this is complete bullshit!&quot; - Rant, 
            belongs in... rants!</font></li>
        </ul>
      </li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>Moderator decisions pertaining to the 
    board are final. </strong> </font> 
    <ul>
      <li><font size="2" face="Arial">When a moderator or administrator makes 
        a decision this choice is not up for public debate.  If you have a problem 
        with the choice made, PM or email that moderator or an administrator.  
        If you create a post to question this choice chances are that post will 
        be locked and you will be issued a warning.</font></li>
      <li><font size="2" face="Arial">Flaming a moderator for a decision publicly 
        will most likely result in a warning or suspension, depending on the seriousness. 
        Remember, flaming does not mean questioning!</font></li>
      <li><font size="2" face="Arial">This rule only applies to decisions involving 
        moderation. Moderators participating in the thread do not have the final 
        say and are considered normal members when posting. Argue and debate with 
        them all you want.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>Post farming is not allowed. </strong> 
    </font> 
    <ul>
      <li><font size="2" face="Arial">Spamming the message board in order to raise 
        your post account is a violation of the rules.  As such anyone found doing 
        this will receive a warning and the posts made will be revoked.  Repeat 
        offenders face suspension and a post count reset.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>Do not bump old posts without good reason. 
    </strong> </font> 
    <ul>
      <li><font size="2" face="Arial">If you’re bumping an old post for a good 
        reason (information was requested or the topic came up again) that is 
        fine.  If you are bumping an old post for no reason or simply to be funny, 
        you are looking at a warning.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>No petitions, please!</strong></font> 
    <ul>
      <li><font size="2" face="Arial">We do not support server-wide petitions 
        to get an issue fixed. This includes but is not limited to petitions to 
        get bugs fixed, issues addressed, and zones fixed.</font></li>
    </ul>
  </li>
</ul>
