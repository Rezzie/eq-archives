<p align="center"><font color="#CCCCCC" size="5" face="Arial"><strong>Signature, 
  Titles, and Avatar Rules</strong></font></p>
<hr width="75%" size="1">
<ul>
  <li><font size="2" face="Arial"><strong>Both your signature and avatar must 
    not be larger than a <em>combined size</em> of 30k.</strong></font></li>
  <li><font size="2" face="Arial"><strong>Avatars and signatures must be appropriate 
    for an EverQuest-related forum. </strong> </font> 
    <ul>
      <li><font size="2" face="Arial">Your avatar or signature may not contain 
        the following… </font> 
        <ul>
          <li><font size="2" face="Arial">Explicit sexual images.</font></li>
          <li><font size="2" face="Arial">Disturbing images.</font></li>
          <li><font size="2" face="Arial">Drug related images.</font></li>
        </ul>
      </li>
      <li><font size="2" face="Arial">If a moderator or administrator feels an 
        image is inappropriate even though it may not fall under the above it 
        may be removed.</font></li>
      <li><font size="2" face="Arial">Recreating the avatar after it is removed 
        will result in either a warning or suspension and your privileges to use 
        an avatar will be revoked.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>Signatures are signatures, not a life 
    story. </strong> </font> 
    <ul>
      <li><font size="2" face="Arial">Signatures should meet the following rules… 
        </font> 
        <ul>
          <li><font size="2" face="Arial">At or under 30k.</font></li>
          <li><font size="2" face="Arial">At or near 468x60 in size.</font></li>
          <li><font size="2" face="Arial">Does not distort the thread.</font></li>
          <li><font size="2" face="Arial">Does not take up a ridiculous amount 
            of space in a post.</font></li>
        </ul>
      </li>
    </ul>
  </li>
  <li><font size="2" face="Arial"><strong>Signatures should only be posted once 
    in a thread.</strong></font></li>
  <li><strong><font size="2" face="Arial">Titles must be appropriate for an EverQuest-related 
    forum.</font></strong>
    <ul>
      <li><font size="2" face="Arial">When you reach 600 posts you can assign 
        yourself a custom title. This title must be acceptable for this forum. 
        It should not and may not contain vulgar language. Inappropriate titles 
        will be removed and the privilege to use a custom title may be taken away.</font></li>
    </ul>
  </li>
</ul>
