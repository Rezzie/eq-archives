<br><ul>*** Headlines ***<BR>
--------------------------<BR>
Eyes on Nektulos - From the saddle of her unholy steed, Lanys T`Vyl, the child of Innoruuk, surveyed the trolls' as they marched with all of their belongings and wares from Neriak, the dark elf city they called home for a time, to the city of Grobb in the Innothule Swamp. The frogloks had finally lost their foothold in Grobb and now the smelling, foul race of trolls had their home back.<BR>
<BR>
She kicked her steed into motion and made haste through Nektulos Forest making a cursory survey of the lands and then turned to Neriak where dark fates would soon unfurl by her ebony hand.<BR>
<BR>
<BR>
<BR>
*** Spells ***<BR>
------------------------------------------------<BR>
- Increased the duration on Divine Avatar.<BR>
- Reduced the casting time on several swarm pet AAs. <BR>
- Modified Intensity of the Resolute so it won't be overwritten by other, lesser abilities.<BR>
- Using Cyclone Blade or Whirlwind Blade will no longer cause AE Taunt to be used up as well. <BR>
- Telekara is now slightly harder to resist, giving it an equal resist chance to other spells in the same line. <BR>
- Nature's Blight and Season's Fury now have 200 spell casting range.<BR>
- Rhythmic Reflexes and Superior Rhythmic Reflexes had their double attack values switched. This has now been corrected.<BR>
- Nature's Blight and Season's Fury will now not focus level 1 spells, including the Shadowknight ability Harmtouch.<BR>
<BR>
<BR>
<BR>
*** Items ***<BR>
------------------------------------------------<BR>
- The dark side of the froglok’s can now wear the armor their culture made. <BR>
- Modified the stats of numerous pieces of the cultural armor and the corresponding symbols.<BR>
- After much chafing, and a few cracked ribs, the froglok have decided that gnome cultural armor is no longer wearable. <BR>
- Blessed Reviver's Charm should now calculate its stats correctly if a non-wis caster is added to the group or raid.<BR>
- All spell scrolls should now be stackable. Please note that if you try to scribe more than one spell scroll at one time by using a stack of spell scrolls, that you will receive an error message. You must use only one spell scroll to scribe a spell. <BR>
- Ancient Mist Root Stone, Refracted Dusk Amethyst, Ancient Life Root Stone, and Refracted Dawn Amethyst are now usable in the hands and wrist slots. <BR>
<BR>
<BR>
