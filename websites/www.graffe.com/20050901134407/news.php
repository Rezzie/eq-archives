*** Headlines ***<BR>
----------------------------------<BR>
Gukta is no more. The Trolls have retaken their ancestral home and torn down the remembrances of the Frogloks. The Guktans, suffering more than just defeat, have retreated into the Rathe Mountains. They have built a tent city to accommodate their needs, but there is something obviously wrong. Many of their friends and family were touched by the curse laid upon the battlefield by the Warlord Ykesha. These poor souls have been corrupted. They have been torn from the breast of Mithaniel Marr and grasped by the clutching claws of Innoruuk and turned to evil. Yet they are cousins and brothers to the remaining Guktans and so they live together, trying to make the best of their fate.<BR>
<BR>
<BR>
*** Items ***<BR>
----------------------------------<BR>
- Ancient Mist Root Stone, Refracted Dusk Amethyst, Ancient Life Root Stone, and Refracted Dawn Amethyst are now usable in the hands and wrist slots.<BR>
- All potions that were recently introduced now have a required level instead of a recommended level.<BR>
- Lightning Singed Mantle now has 38 AC.<BR>
