 <BR>
*** AAs ***<BR>
 <BR>
- Gift of Mana will no longer be used up when casting a 0 mana spell after it has triggered on you.<BR>
- Gift of Mana should now have a chance to trigger on heals other than just instant heals, including heal over time and percent healing spells.<BR>
<B>- The way that the wizard AA Mana Burn works has been modified.  The new version will use a maximum of 3000 mana instead of draining your whole pool and will do 4 damage for every point of mana spent.  This is a raw channeling of magical energy that cannot be focused, and will not crit.  The ability is unresistable by normal creatures, but certain extremely powerful creatures may be immune to the effects.  Like the original version, once a Mana Burn ability has affected a target, no other mana burn abilities will be able to affect that target for the next minute.  Upgraded versions of the ability, Mana Blast and Mana Blaze, are available at level 65 and level 70 and use a maximum of 6000 and 9000 mana respectively.  The original Mana Burn ability has been refunded and can be repurchased by Wizards interested in the new version. </b><BR><br><br>-Qaman</ul></div>
