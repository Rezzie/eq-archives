<a href="#" onclick="toggleView('notab', 'main205', 2);return false;" title="Click to Expand">Patch Message - 7 Dec 05</a> &raquo;
</td><td class=menuhead nowrap>December 7th, 2005</td></tr><tr><td class=menudata colspan=2>
<div id="main205" style="display:none;">
<br><ul>December 7th, 2005<BR>
_____________________<BR>
<BR>
<BR>
*** Headlines ***<BR>
<BR>
~ Holiday Festival ~<BR>
<BR>
M.C. Tinkerton needs help repairing his Clockwork Party Machine in time to celebrate 3199! The party machine's dance gyroscopes, firework cannons and party favor dispensers are fully functional, but the most important piece, the machine's primary party motivator, was stolen at the zero hour. M.C. Tinkerton seeks adventurers to steal back the primary party motivator from the Dark Mistletoe giants, a grouchy clan of frost giant nomads in the Great Divide. Seek out M.C. Tinkerton in the Plane of Knowledge for further details. <BR>
<BR>
~ New Arena ~<BR>
<BR>
A new arena zone is now available off of Lake Rathe!<BR>
<BR>
<BR>
*** Tradeskill ***<BR>
<BR>
- Shield of Maelin now uses the proper quill.<BR>
- The following LDoN spell research recipes have been disabled: Death's Despair, Instinctual Panic, Eidolon Bellow, Soulless Panic, Angstlich's Echo of Terror, Jitterskin, Unholy Bellow, Shadow Bellow, Call of the Muse, Guidance. <BR>
- Jar of Clockwork Grease is now made with a Small Grease Jar in a toolbox. A Small Grease Jar is made the same way a clay jar is, except that you use a Small Grease Jar Sketch. <BR>
<BR>
<BR>
*** Events, Missions <BR>
<BR>
- Additional text has been added to "Investigate a Disappearance" that should help clear up a confusing spot late in the task.<BR>
- Soulseeker, Death's Sigh can no longer drop more than once per kill.<BR>
- The Mask of the Hidden quest in the Ascent now resets properly after 1 hour of inactivity. <BR>
- Monster Mission experience now properly takes into account hell level modifiers. These modifiers smooth out the experience curve to lessen the impact of the old hell levels (30, 35, 40, and 45). Inclusion of these modifiers in the monster experience will cause you to gain more experience than before during hell levels, essentially returning it to a normal level, but you will gain slightly less experience than before when you are not on a hell level. <BR>
- Within Monster Missions, you will now find Shards of Wisdom that will impart upon you the experiences of the creature you inhabit at the time. Looting one of these shards will grant the experience to your main character upon successful completion of the mission.<BR>
- Aid Fimli in the Plane of Knowledge will now offer to trade you a Cloak of Lesser Pernicity for a Cloak of Greater Pernicity. This is for players who wish to have the original cloak instead of the new, attunable cloak. <BR>
- Clockwork Commander Cogswain has outfitted his clockwork models with upgraded weaponry for the War Machines monster mission that begins in Lavastorm. He only has a limited supply of Clockwork Models in commission, so be careful with them. <BR>
- Farsoth Mal has regained some of his power deep within the Lair of Splitpaw. Defeating him may prove a bigger challenge than before!<BR>
- Dinner invitations to Dreadspire Keep are now flame-resistant and reusable.<BR>
- The Performer event will now respawn properly after a failure.<BR>
- The respawn time on many NPCs in the Demiplane of Blood has been significantly increased.<BR>
<BR>
<BR>
*** Items ***<BR>
<BR>
- Archaeologist Flottwit has uncovered a cache of older Darkhollow items and is currently searching the bazaar for some of the newer items for comparison. If you have a Depths of Darkhollow item that changed from tradeable to no trade with the last patch and you'd prefer to have the old tradeable item instead, give the no trade version to Flottwit and he'll give you the old tradeable item in return. Flottwit will only be around until the next patch. <BR>
- Heartlight is now no-drop, poof-on-death, and no-rent.<BR>
- The mixing bowl and oven now identify Rockhopper eggs more clearly. <BR>
- The Cultural Charms should now be tradeable on Firiona Vie. <BR>
- Sporali Battle Hammer of Spirits and Daybreak, Bringer of Light have had their proc chances modified.<BR>
- Vule's Frozen Heart and Frozen Heart Shards now have +30 atk, +3 accuracy, and reduced weight.<BR>
- The hitpoint and mana values on the following items have been increased: Ring of Dark Mists, Dark Void Shoulders, Jagged Coral Choker, and Pristine Illsalin Stonecraft Mask. <BR>
- Some of the Depths of Darkhollow hp/mana augmentations were increased too much in the last patch and have been reverted to their old values.<BR>
<BR>
<BR>
*** NPC ***<BR>
<BR>
- Hatchet the Torturer will now always leave a corpse when slain. <BR>
- Nitram Anizok has become interested in some of the clockwork that are near the Plane of Innovation entrance. He will now wander from his workshop and explore the entry to the plane for a few minutes before returning to his workshop.<BR>
<BR>
<BR>
*** Titles ***<BR>
<BR>
- Corrected an issue that prevented server-based suffixes for the Cazic Thule server from being displayed properly. <BR>
<BR>
<BR>
*** Spells ***<BR>
<BR>
- Renamed "Zombie Malaise" to "Wurine Guard" and "Wurine Guard" to "Zombie Malaise". This is to correct an inconsistency in the naming. <BR>
- Corrected the spelling of "Thief" in the description for the spell Enduring Vision.<BR>
- Capitalized "Eyes" in the spell Thief's Eyes.<BR>
- Fixed issues with the Aura of Arcanum spells to allow better stacking with other resist buffs.<BR>
- Summon Companion will now summon a pet even when you have Steadfast Servant up.<BR>
- Field of Vision spells will now stay persistent through zoning.<BR>
<BR>
<BR>
*** AAs ***<BR>
<BR>
- Gift of Mana will no longer trigger on any spell that costs no mana to cast.<BR>
- The monk AA Eye Gouge is now more effective and will stack better with other attack debuffs.<BR>
- The magician AA Elemental Agility should now properly cause your pet to be hit less often.<BR>
- Guardian of Ro ranks 2 and 3 will now properly increase the damage that your Guardian of Ro does.<BR>
- Attempting to use Living Shield before the cool down is finished should no longer result in an error message.<BR>
- When Bards use the Origin ability it can now be interrupted by ducking.<BR>
- AA's should no longer vanish from your AA list until you zone.<BR>
<BR>
<BR>
*** UI ***<BR>
<BR>
- A bug where the UI could not be reloaded more than a few times has been fixed.<BR>
- The 'Skills' option on the EQ menu has been moved down into the player information sub-menu.<BR>
- Added new EQType label "Target of Target Name", type 135, at the request of the UI community.<BR>
- Added optional "CloseButton" button to the Story Window at the request of the UI community. The default UI doesn't have this button, but if UI creators want it they can add one now.<BR>
- The UI XML parser has been slightly optimized, slower computers may notice a slight decrease in load times.<BR>
- Through a change to defaults.ini, the way the Evolving Item XP Transfer window opens should no longer obscure parts of the window. <BR>
- A Bazaar Search UI bug where the All Effects filter would no longer bring up weapons with combat effects has been fixed.<BR>
- The bandolier window is now persistent across a zone change.<BR>
- Hotkeys in the hotbar as well as in the actions window will remain useable when you exit a monster mission.<BR>
- It is no longer possible to get 2 group leaders in the same group in the raid window.<BR>
- In the Bard Songs Window, all icons should now appear the same size and not overlap each other.<BR>
- Fixed a bug with groups and raids where if group member with a shared task /raidinvites a separate player with a different shared task, the group players become bugged.<BR>
- Fixed a crash related to minimizing the chat window to as small as it can go.<BR>
- Swapping to a weapon configuration that includes a light source in the bandolier should now activate that light source.<BR>
<BR>
<BR>
*** Miscellaneous ***<BR>
<BR>
- Corpses that were under the world when a zone starts up are now moved to the safe spot of the zone. <BR>
- Sharvahl and Hollowshade now use Luclin night settings for the sky. Previously there was no sky in these zones. <BR>
- The /safelock command now prevents the Esc key from altering in game windows while safelock is activated. <BR>
- You can now no longer summon a horse while controlling a boat. <BR>
- If a tracking class enters a monster mission that has a template with track, their sort track will now work correctly afterwards.<BR>
- /Dzadding a character that is zoning should no longer cause various bugs.<BR>
- Various atlas typos have been corrected. Grobb was spelled Gukta; even the trolls could spell enough to tell that was wrong.<BR>
- New Half Elves can now claim Kelethin as their starting city.<BR>
- Fixed a problem most often seen in the Tutorial where Arias would attack the player if they use the hide ability. This was a general bug; any NPCs that were told to follow the player because of a script would agro when the player tried to hide.<BR>
- The '/who all' command will now filter searches for Frogloks.<BR>
<BR>
<BR>
<BR>
<BR>
<BR>
<BR>
- The EverQuest Team<BR>
<BR><br><br>-Qaman</ul></div>
