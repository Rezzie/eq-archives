All New PVP System for PVP Servers * <BR>
_____________________ <BR>
<BR>
The Watch List: <BR>
- Safe Zones: We’’re still looking into a better solution for the problem of safe zones. Expect some kind of change in the future. <BR>
- Point System: There are still concerns of potential exploits with the point system at this time. Therefore the selection of PvP items available on the merchants will be limited to consumable items for now. We’ll monitor the accumulation of points after this update. If all goes well, we will add a selection of equipment to the merchants in the near future. <BR>
- Sullon Tokens: As a secondary concern with the point system, we are delaying the addition of the turn-in quest for Sullon Zek tokens. If there’s a potential problem with the point system that eventually requires us to make adjustments to the way points are awarded, we don’t want players to end up with less than full credit for these tokens. Once the point system has had any potential issues ironed out we will add a quest to turn in your tokens for PvP points. <BR>
<BR>
PvP Combat Changes <BR>
<BR>
- Melee attacks have a 50% increased range. This does not affect thrown weapons or archery. <BR>
- The maximum damage any one spell can do is now capped at 40% of the target’s total hit points, down from 75%. <BR>
- Archery damage has been increased to 80% of its base damage in PvP, up from 66%. <BR>
- Being stunned while on a horse will now cause the horse to disappear. This only applies to stuns caused by other players. <BR>
- Lowered player melee mitigation, effectively lowering Armor Class by 20%. This will increase the damage potential of melee weapons in PvP. Originally the targets full armor class was considered. <BR>
- Player spells now have a separate duration field for PvP. Individual spells will continue to be tuned during beta. <BR>
- Player spells now have a PvP specific resist mod. Spell lines with resist mods differing from their normal adjusts initially include: <BR>
Movement Rate <BR>
Root <BR>
Enthrall <BR>
Mana Drain <BR>
Stuns <BR>
Direct Damage <BR>
Damage over Time <BR>
Pet procs/innates <BR>
- Added higher level snare spells for rangers/druids with increased resists mods. This should allow them to land snare spells with similar frequency to other snare spells. These spells are available on the PvP merchants for 1 point each. <BR>
- Necro and Mage pets have had their PvP resists increased to closer match the existing Beastlord pet resists. <BR>
- Lowered the duration of most snare, Enthrall, and Root spells and abilities. <BR>
- Berserker snare is no longer unresistable. <BR>
- Added a set of pumice stones to the PvP merchants that will cast various forms of dispel magic with a 1.5 second cast, 12 second cast time. <BR>
- Arena deaths will now place you at your bind point, and not in shadowrest. <BR>
- PvP Resurrection effects now last 5 minutes + 1 minute/negative vitality point. The Max duration is now 15 minutes instead of 25. <BR>
- Made further changes to prevent hill ghosting. Please test this and give feedback. <BR>
- Ranged weapons are no longer restricted by the Z axis, allowing greater freedom of use on varied terrain. <BR>
- Snare will now properly slow mounts. The mount will still be faster then normal running while snared, but will considerably decrease their movement rate. <BR>
- Spells now have a minimum chance of landing of 5%, up from 2%. <BR>
- Water will no longer cause you to be immune to people outside of water and vice versa. <BR>
- Zoning with low health will no longer set your hit points equal to the amount of +HP gear you are wearing. <BR>
- Players killed in PvP combat will now spawn in Shadowrest with full equipment. A naked corpse will remain in the location they died for 3 hours. Note that this effectively removes item loot from Rallos Zek. With the addition of no drop augments it became rather trivial to effectively remove item loot from the server anyway, and we felt the benefits of the new death system outweigh the benefits of an item loot system. <BR>
- Players who are killed in PvP combat will respawn with special resurrection effects on them. The duration of these effects is variable dependant on how frequently they die. <BR>
- Summon Corpse Potions are now usable by any class/race. <BR>
- A system to score PvP kills has been implemented. For more information see below. <BR>
<BR>
Scoring System <BR>
<BR>
- The new scoring system will award points to any player who kills another player. If the player is grouped the points will be split between the players in the group. The points awarded will be based on three things: <BR>
<BR>
1. Level of the killer <BR>
<BR>
2. Scoring Modifier <BR>
<BR>
a. Level Difference (Each kill has a base point cost determined by the level of the killer. All subsequent modifiers apply to this base number.)For each level the killer is above or below the victim, the scoring modifier increase by one point. If you kill some one who is higher level than you, you’ll gain more points than you will for a lower level character. You will also subtract points from your modifier for killing a player lower level then you. <BR>
<BR>
b. Infamy Difference - Each time you kill another player, points are added to your infamy pool. At certain thresholds a player will gain an infamy level. When a player is killed, the infamy levels are compared and the killer gains a scoring modifier point for each infamy level his victim is above him. He can also lose scoring modifier points for killing someone who is of lower infamy. Infamy will gradually deteriorate while the player is online. <BR>
<BR>
c. Vitality score - When a player is killed their vitality is set to –10. For each 5 minutes they are alive, they will gain back two points of vitality. This vitality total is added directly to the scoring <BR>
<BR>
d. Group bonus - Groups of 4 or more will receive a bonus % of points for each kill. 4/10%, 5/15%, 6/20%. <BR>
<BR>
3. Time since the victim was last killed by the same player. Players that repeatedly kill the same player will see a steep decrease in the amount of points a player is worth. They will gain half the points for the kill they normally would for each time they kill that player in a 24 hour period. After the score has dropped below 1 point, they will begin to lose points for killing the same player. <BR>
<BR>
Example: Frizznik kills Rytan. Frizznik is level 60, while Rytan is level 62 for a level difference of 2. Frizznik has an infamy level of 5, while Rytan has an infamy level of 2, which results in infamy difference of –3. Rytan has not recently been killed, so his vitality is 0. Level Difference + Infamy Difference + Vitality = Scoring Point Modifier or 2 + (-3) + 0. <BR>
<BR>
- Each scoring modifier point adds or subtracts 5% to the base score. Normally Frizznik would gain 100 points for killing Rytan, but because of the scoring point modifier he will gain 95 points instead. <BR>
<BR>
<BR>
Leader Board <BR>
