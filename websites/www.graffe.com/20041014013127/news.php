<BR>
<BR>
Headlines<BR>
-------------------<BR>
Omens of War is live!<BR>
New Teleportation Spells to Barindu.<BR>
Just me and my shadooooow!<BR>
Reflective Water?<BR>
Register now for the New Orleans Fan Faire.<BR>
<BR>
Omens of War<BR>
-------------------<BR>
Explore the war torn lands of Kuua and reap the benefits of new AAs, spells and items including the new, powerful, Epic 2.0. Lush new areas, filled with beautiful creatures and vibrant, living landscapes await you at every turn. We hope you enjoy all that EverQuest: Omens of War brings to the game.<BR>
<BR>
Travel to the Omens Zones<BR>
-------------------<BR>
The Priests of Discord in Norrath believe they have been granted a gift. A unique knowledge has been bestowed upon them which they believe was granted as a reward for preaching the way of Discord. That gift, they claim, is the knowledge of a way to a world that is enveloped by Discord itself -- an ethereal poison that wreaks havoc and strife upon all it touches.<BR>
<BR>
Speak to a Priest of Discord to learn how to enter the World of Kuua.<BR>
<BR>
The Task System<BR>
-------------------<BR>
Only have a short time to play or are you waiting for a group or raid? Immerse yourself in a Task. You can find Task Masters all over Norrath. They can be found in most home cities as well as in places like the East Commonlands, the Butcherblock Mountains, Toxxulia Forest and the Swamp of No Hope. For the more daring individuals, the most challenging tasks can be found by speaking with Task Masters in the Dreadlands and the Overthere.<BR>
<BR>
You can open the task window by hitting Alt+Q and start enjoying this new game-play style and enhancement to the EverQuest experience.<BR>
<BR>
Be mindful of turning in task items in stacks, as with all quests in EQ, please seperate the items before turning them in for a reward.<BR>
<BR>
Epic Weapons 2.0<BR>
-------------------<BR>
Will you be the first of your class to earn these powerful new items?<BR>
<BR>
Guild Tribute<BR>
-------------------<BR>
Omens of War expansion brings a new Guild Tribute system. This system will allow guild members to donate items/cash for Tribute points, and allow officers to select benefits that the entire guild will receive, deducting the cost from the guild’’s tribute pool.<BR>
<BR>
Guild Tribute is an extension of the tribute system introduced in Gates of Discord and purchasers of either expansion, will be able to reap the new benefits.<BR>
<BR>
Retail Claim Item<BR>
-------------------<BR>
Those of you who purchased the retail version of EverQuest: Omens of War will need to use the /claim command in-game to receive your in-game items. Enjoy!<BR>
<BR>
Select your title<BR>
-------------------<BR>
You can hit Shift+T to bring up the new Title selection UI which will allow you to choose from the titles you have earned, through AAs, levels or accomplishments, to be shown with your character name in-game.<BR>
<BR>
<BR>
Voice Macros<BR>
-------------------<BR>
Using Ctrl+V will bring up the new Voice Macro UI. There you will see a list of available voice macros as well as the options of Play, Tell, Group and Raid. Selecting a macro and play will play the macro for you only. Selecting a macro and using one of the other options will send the voice macro to either a tell (using the person you have targeted as the recipient), group or raid.<BR>
<BR>
To create your own voice macros, make a sound file using the same name as one of the existing voice macros, which can be found in your EverQuest>Voice>Default directory and place it in your EverQuest>Voice>User directory.<BR>
<BR>
You can create hot keys of your favorite Voice Macros by selecting the macro you would like and clicking and holding on the Tell, Raid or Group button until the icon appears on your cursor.<BR>
<BR>
We hope you enjoy this new way to immerse yourself into the EverQuest experience.<BR>
<BR>
<BR>
New Spell Slot<BR>
-------------------<BR>
For the first time in EQ, you can open up a new spell slot by learning the Mnemonic Retention AA ability. 9 Spell slots! Wow, Why I remember in the early days of EQ, we only had 8 spell slots and we liked it. We cast spells all day, going uphill both ways. You whippersnappers have it easy =)<BR>
<BR>
More Buff Slots<BR>
-------------------<BR>
If you find that you need more room for beneficial buffs on your character, activate the Mystical Attuning AA and open up more room in your buff slot window.<BR>
<BR>
Improved Customization for Graphics<BR>
-------------------<BR>
-There are now 3 sets of options (Spell, Environment, and Actor). All 3 sets have similar options that affect each type of particle. These options allow for more customization when setting up your display and filters.<BR>
<BR>
-An "advanced" button has been added under the display tab in the options window. This will allow you to toggle some of the new settings, for our new customizable graphics and effects. You will most likely need to expand the options window to see the new Advanced button.<BR>
<BR>
- Shadows; Toggles the new dynamic shadows.<BR>
- Water Swap*: Switches between old style water, and new reflective water the next time you zone.<BR>
- 2.X Pixel Shaders: Toggles the use of pixel shaders 2.0.<BR>
- 1.X Pixel Shaders: Toggles the use of pixel shaders 1.0.<BR>
- Vertex Shaders: Toggles the use of vertex shaders.<BR>
- Sky Reflection Type*: Set to Static to use a preset reflection or Dynamic to have the sky reflected on the water.<BR>
- Dynamic Update Interval: Control the CPU costs of the dynamic reflection by adjusting how often to update the reflection texture<BR>
- Dynamic Sky Reflection Size*: Allows you to control how large the reflection texture is.<BR>
(The * indicates a setting that will take effect next time you zone).<BR>
<BR>
<BR>
- You can now see real time shadows within the world of Norrath. Use the shadows option in options>Display>advanced to turn them off and on. Now, even when soloing, you will never be alone. Those of you with older video cards may have some performance issues when attempting to use shadows. If your machine does not meet the recommended system specifications, we suggest that you play with shadows off.<BR>
<BR>
- Enjoy sunsets and moonrises viewed above EverQuest's new reflective water which is available for everyone, wherever there is static water, e.g. Oceans, seas and lakes. You can toggle the new water shaders on and off using the Water Swap button, under options>display>advanced.<BR>
<BR>
Zoning Screen<BR>
-------------------<BR>
When zoning, you will now see the loading splash screen. You have not crashed, do not be alarmed.<BR>
<BR>
<BR>
New Improved Particles<BR>
-------------------<BR>
-You will now be affected by the environment around you, be sure to check out these effects especially under water, in frigid weather, dry climates and upon death.<BR>
<BR>
Spells<BR>
-------------------<BR>
- Clerics Symbol and Aegolism spell lines no longer require reagents.<BR>
- Necromancer pets will no longer require peridots.<BR>
- The beastlord spell, Tureptan Spirit, now has a healing component.<BR>
- New transportation spells are available in the Abysmal Sea. Check with your transportation spell contact for more information.<BR>
- Bard PBAoE spells will no longer damage targets that are moving, however any other spell effects from the PBAoE spell will still be applied to the targets as usual.<BR>
- Beneficial spells can now be used in the EverQuest tutorial.<BR>
<BR>
Skill Cap Changes<BR>
-------------------<BR>
- Piercing now caps at 250 at level 60 for warriors, rangers and beastlords.<BR>
- 1h Blunt and 2h Blunt now cap at 250 at level 60 for beastlords<BR>
<BR>
Items<BR>
-------------------<BR>
- The Timespinner pet will no longer cast spells.<BR>
- The Dagger of Death has had its AC value reduced.<BR>
- Some of the new Omens of War items will now display an "Attunable" attribute, which means that they can be traded freely until they are equipped or augmented. Attunable augments will become no-drop flagged when added to an augment slot, attunable items will become no-drop when equipped. You will not be able to auto-equip an attunable item that has not been attuned to your character. Please be mindful of the new Attunable flag before equipping or augmenting new items.<BR>
<BR>
Alternate Advancement Abilities<BR>
-------------------<BR>
- Berserkers have received some new AAs. This should bring their AA selection up to par with comparable melee classes.<BR>
- Added the ability for shadow knights to train the Gates of Discord ability Improved Theft of Life and the Planes of Power ability Suspended Minion.<BR>
- The healing wards that priests can summon using their Gates of Discord AAs are now immune to most spells.<BR>
- Lowered the reuse time on the Magician ability Host of the Elements to 15 minutes.<BR>
- Added the ability to train Quick Damage to Magicians as a Luclin Class ability.<BR>
- Increased the critical hit chance for the Wizard Gates of Discord AA Frenzied Devastation.<BR>
<BR>
Fan Faire<BR>
-------------------<BR>
We are proud to announce the Halloween, New Orleans Fan Faire! The event dates are October 28-30, 2004, and space is going fast! The last day to guarantee your spot by pre-registering is October 11th. For more information, please go to http://everquest.station.sony.com/fanfaire/ See you in New Orleans!<BR>
