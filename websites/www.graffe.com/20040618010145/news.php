</td><td class=menuhead nowrap>June 16th, 2004</td></tr><tr><td class=menudata colspan=2>
<div id="main201" style="display:none;">
<br><ul>June 16th, 2004 <BR>
----------------------- <BR>
<BR>
Headlines! <BR>
<BR>
-Collision sees more improvements. <BR>
-A portal to Gunthak has been added to the Plane of Knowledge. <BR>
-/guidehelp is now available! <BR>
-Gates of Discord sees more changes. <BR>
<BR>
<BR>
Collision Updates <BR>
----------------------- <BR>
Many remaining collision issues have been fixed. <BR>
-Floating NPCs in the Plane of Disease should be more down to earth. <BR>
-NPC warping should be reduced. <BR>
-The Emerald Jungle should no longer have NPCs below the world <BR>
-You should once again collide with the dock in Natimbi. <BR>
-The entrance to Najena should be easier to navigate. <BR>
<BR>
Gunthak Portal <BR>
----------------------- <BR>
-We have added a tome to the Plane of Knowledge to allow for faster travel to and from Gunthak. Enjoy. * <BR>
<BR>
Gates of Discord <BR>
----------------------- <BR>
-Increased the drop rate of quest items, for the Breakdown in Communication quest, in the sewers and mountain passes and Ikkinz. * <BR>
-Increased the number of items that drop from the Qinimi and Barindu events for the Breakdown in Communication quest. * <BR>
-Increased the drop rate of Breakdown in Communication quest items off the Yxtta bosses as well as making it possible to get them off other NPCs in the zone. <BR>
-The Ikkinz Chamber of Might and Tipt boss mobs will now behave a little smarter when facing a strong defense. Beware! <BR>
-Some tuning changes have been made to the Ikkinz single group trials and Tipt. <BR>
-Changed the NPCs that guard Txevu so they are a longer re-spawn allowing you to get stragglers thru the zone much easier once you kill them. <BR>
-Pathing in Txevu should be better. * <BR>
-The bonus reward at the end of Inktu`ta will spawn in a chest instead of being handed directly to the player that triggers the dialogue. <BR>
-Gates NPCs that dropped spells will now drop a token, which can be turned in for a class appropriate spell. * <BR>
<BR>
The Plane of Time <BR>
----------------------- <BR>
-We have made changes to the scripting for the Plane of Time, which will allow the server to shut down unused instances of the zone. This will remove the problem of waiting for an instance that another group has finished to shut down, before you can start your attempt. * <BR>
<BR>
The Plane of Hate <BR>
----------------------- <BR>
You can now access the Plane of Hate by talking to Relm M'Loch in the Plane of Tranquility and handing her a Fuligan Soulstone. * <BR>
<BR>
Items <BR>
----------------------- <BR>
-The Tainted Axe of Hatred has had its damage increased. <BR>
-Many Gates items have had their weight reduced. * <BR>
-The Muramite Death Shroud is now equipable by beastlords and has a wind instrument modifier. * <BR>
-The Onyx Ring of Prayer now has a stringed instrument modifier. * <BR>
-The drop rate has been increased for the following items in the Plane of Hate * <BR>
The Shattered Emerald of Corruption <BR>
Decrepit Hide <BR>
Staff of Elemental Mastery: Earth <BR>
Essence of Vampire <BR>
Eye of Innoruuk <BR>
-The Diamond Tipped War Javelin should now look more javelin like. * <BR>
<BR>
Berserkers <BR>
----------------------- <BR>
-Berserker snare now has a set duration. * <BR>
-Berserkers should no longer get stuck during their epic quest. <BR>
-The following effects now lower hate instantly rather than over time: * <BR>
Diversive Strike <BR>
Distracting Strike <BR>
Confusing Strike <BR>
<BR>
Guidehelp <BR>
----------------------- <BR>
-The "/guidehelp" command is now available. The /guidehelp command is for quest/event feedback, wedding requests and questions regarding quests/events. Please be aware that all Customer Service issues must go through /petition for resolution. There may only be 1 request in the guidehelp queue at a time for each player. <BR>
<BR>
Raid Management <BR>
----------------------- <BR>
-Raid leaders now have the ability to add notes to the raid window. There is now a tabbed window in the Raid window, clicking on the Notes tab will show all the raid members in a single list, with notes next to their name. The Raid leader can click on a player, and change that player's note in the edit box below the list. The note will show on all raid members' raid windows. * <BR>
<BR>
-Added an MOTD to Raid Window. On the Notes tab there is a window for an MOTD at the bottom. The Raid leader can edit this MOTD, and the new text will show on all Raid members' Raid windows. * <BR>
-The escape key will now close your Raid Window. <BR>
-Raid Leaders can now remove members from the raid, even across zones. * <BR>
<BR>
Tradeskills <BR>
----------------------- <BR>
-There have been several upgrades to Gates Tradeskilled items. * <BR>
-Gates smithed weapons have been adjusted. * <BR>
-Gates fletched arrows have had their damage increased and are now magical. * <BR>
-Tradeskill drinks from Gates of Discord have had their duration enhanced. * <BR>
<BR>
Stopcasting <BR>
----------------------- <BR>
We have added a /stopcast command -- This command will not work when riding a horse. * <BR>
<BR>
Undead Pets <BR>
----------------------- <BR>
Pets no longer go away after their master has feigned death for more than 2 minutes. * <BR>
<BR>
Lay on Hands and Harm Touch <BR>
----------------------- <BR>
Lay on Hands and Harm Touch will no longer affect corpses. * <BR>
<BR>
Corpse Dragging <BR>
----------------------- <BR>
We have increased the range of the /corpse command * <BR>
<BR>
Spells <BR>
----------------------- <BR>
Many damage shields have had their damage and duration increased. * <BR>
Increased the regeneration rate on the following spells: <BR>
-Regrowth of the Dar Khura <BR>
-Regrowth of the Grove <BR>
-Regrowth <BR>
-Replenishment <BR>
-Blessing of Replenishment <BR>
<BR>
Modified the heal rate for the following spells: * <BR>
-Supernal Elixir <BR>
-Quiescence <BR>
-Holy Elixir <BR>
-Breath of Trushar <BR>
-Supernal Cleansing <BR>
<BR>
Lowered the mana cost on the following spells: * <BR>
-Black Steel <BR>
-Ancient: Chaos Vortex <BR>
-Sun Vortex <BR>
<BR>
Increased the duration and mana cost on the following spells. * <BR>
-Falcon Eye <BR>
-Eagle Eye <BR>
-Hawk Eye <BR>
<BR>
Bards <BR>
----------------------- <BR>
Bards should no longer use instrument animations when wielding items other than instruments. (No more stabbing yourselves in the head with your Epic!) <BR>
<BR>
Character Creation <BR>
----------------------- <BR>
The character creation process now has Voice Over to guide you through new character creation. <BR>
<BR>
Mouselook <BR>
----------------------- <BR>
Mouselook is much smoother and variable via a slider in the options window. If you use mouselook, make certain you adjust the new settings. <BR>
<BR>
Tutorial <BR>
----------------------- <BR>
All characters should start with the tutorial bank items. <BR>
<BR>
<BR>
Filters <BR>
----------------------- <BR>
-The Focus effect should now filter haste messages. * <BR>
-The Damage Shield filter should now work correctly. <BR>
<BR>
-- The EverQuest Team <BR>
<BR>
* Denotes an item that was requested by the EverQuest community. <BR><br><br>-Gemdiver</ul></div>
