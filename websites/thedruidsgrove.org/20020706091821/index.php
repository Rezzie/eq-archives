	This years <a href=http://www.wizards.com/default.asp?x=gencon>Gen Con</a> will take place August 8-11th. Among all the other games going on, there will be daily EQ competitions starting at 11 am. The first event will be a Warrior vs. Warrior battle to the death using equal items. The second event will give you and your friends a chance to compete as a team in a 3 on 3 Gladiator Competition. 

