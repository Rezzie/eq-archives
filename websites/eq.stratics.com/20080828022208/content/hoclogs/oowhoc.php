<br><p>EQ Stratics is pleased to bring you the House of Commons Chat Log with the Omens
       of War Expansion Team on August 24th, 2004.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong>&nbsp;</font> Welcome 
      to another great House of Commons chat with the developers of Everquest! 
      The topic will be Omens of War so please try to ask questions around it. </p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> Please 
        send said questions to [QT]Brekkee or [QT]Gowel or [QT]PhroG - to do so 
        type /query NICK and type in the window that pops up. Questions sent to 
        myself or the developers will NOT make it into the lineup, so don't bother. 
        Full logs of the chat will be up on http://eq.stratics.com shortly after 
        the chat.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> So without 
        further ado we'll start with a brief introduction from the developers, 
        now would be a good time to send those questions.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> Hi everyone, 
        I'm Ryan Barker a senior game designer on EverQuest</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> Hey everyone. 
        I'm Travis McGeathy, one of the game designers on EverQuest.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Howdy, I 
        am ALan Crosby also known as Brenlo</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> and sometimes 
        I go by Hey you</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Melisande_Luclin* 
        Are there things in OoW that will be for all levels other than the Task 
        System?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> good question</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Rytan is 
        thinking up an answer</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> The task 
        system is the only content of the expansion that&#8217;s geared towards 
        &#8220;ALL&#8221; levels but it&#8217;s very far reaching. The system 
        has a lot of content that causal players are going to be able to enjoy 
        at all levels.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> His keyboard 
        is on fire</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> The new zones 
        for the expansion are geared for levels 45-70 and generally accessible 
        to all players. There&#8217;s only one keyed zone in the expansion. It&#8217;s 
        a lot like Velious in that regard.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Subu* 
        What are the plans for continued development of Everquest after OoW is 
        released or will there be a major push for Everquest 2?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> We have 
        already planned out the next 18 months of EQ development and have lots 
        planned for the future. EQ and EQ2 will run concurrently and there are 
        no plans to make anyone switch games. We want you to continue to enjoy 
        EQ for years to come.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> (Please 
        remember to focus your questions to the Omens of War expansion when sending 
        to our QT's!)</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Pumra* 
        will a level 1 be able to use the task system?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> You actually 
        do have to be at least level 3 to get a task, but with the new tutorial 
        you&#8217;re probably going to be level 5 before you even get out of the 
        tutorial. So it&#8217;s really available to everyone almost immediately.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Fanon_EMarr* 
        Will we continue to see rune-style spell turn-ins in OoW or will there 
        be a return to the actual dropped spells? In addition, will any spells 
        besides transit-relate be available from vendors at release?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> The spell 
        acquisition system for Omens is going to be a lot like the PoP system, 
        except that you&#8217;ll get a different rune for each level set, and 
        you won&#8217;t get repeats of the same spell when you turn them in. We 
        don&#8217;t plan on having any non-travel spells on vendors at release.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Bigfan* 
        Once OoW goes live, where should I go, or what should I do first?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Where should 
        you go? Whereever you want =) Provided you are of appropriate level. You 
        could start on your epic 2. Do some tasks. Try some of the new tradeskill 
        recipes or start working on the new AAs or levels. Personally, I will 
        be doing a few tasks.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Grizumpy* 
        In OoW is there going to be a wider variety of augments, for some focii 
        that were left out of God, Currently there are only a few items that &quot;Extend 
        Dot Duration&quot; and they are all in Planes of Power, Will OoW look 
        to address the lack of certain focus/augs in GoD?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> Augments 
        as well as traditional gear will have the foci available on a number of 
        new pieces of equipment. Omens should offer a greater variety of items 
        available for any foci that you&#8217;re looking for.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Auricula* 
        Will OoW have content soloable to level 70 for casual players who are 
        not elemental-equipped. Will there be soloable instanced zones?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> We will 
        not be adding any solo instanced features at this time. It is something 
        that we are exploring but would take a tremendous amount of work and is 
        not something we could do rapidly.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *MrGnomer* 
        How tough will Magi pets be compared to now and how will current focus 
        work on them?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> Mage pets 
        are getting a solid boost from 66-70. There&#8217;s several mages in beta 
        that are from the mage&#8217;s tower that have been working with me to 
        help tune the pets for omens. This is also true of the necromancer, beastlord, 
        shadowknight, shaman, and enchanter pets. Current foci will continue to 
        work on the new pets.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Darcreaper_7th* 
        how does the tasks work and what do you get out of it once finished a 
        task</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font>Tasks put 
        together a few elements that may require you to kill something, explore 
        an area, or hail a certain NPC. Each time you will have a change of getting 
        one of 3500 different tasks that result in an xp reward and perhaps some 
        cash.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Guest36* 
        How much did input from the players factor in to the content of OOW (AA's, 
        zones, ect.)</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> Player 
        input plays an important role in the design of any expansion. For Omens 
        of War in particular, the feedback we received from players during previous 
        expansions played a large role in the overall design of the expansion.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> The players 
        in beta and on test are one of our most valuable resources when creating 
        and tuning content.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> we've got 
        lots of players that we're constantly talking to in regard to all aspects 
        of the game.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font>In fact, 
        after gathering your feedback the team took many of the designs back to 
        the drawing board and reworked much of the expansion. All thanks to you 
        and your input.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *SDKSnowboy* 
        Several rumors have been afloat as to leveling from 65 to 70. Will advancing 
        to level 70 be based purely off of standard XPing, and will there be any 
        Requirements, AA or otherwise, to achieve levels.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> Leveling 
        from 65-70 will be exactly like leveling from 60-65. Traditional experience 
        filling that little yellow and blue bar that you all know and love. </p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *zabab* 
        Are you going to give ranger mgb AA?</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> There are 
        no plans at the moment to add Mass Group Buff to the Ranger AA list.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Sorry Rangers</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Sobewiz* 
        will OOW offer &quot;piggy back&quot; into zones that some members or 
        recruits may not be flagged for, much like the Elemental Planes ?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> Yes there&#8217;s 
        plans to allow recruits to be added more easily to the locked zone in 
        Omens. I don&#8217;t want to go into detail as it&#8217;s not finalized 
        yet, but it should be big improvement over what you had to do in the past 
        to backflag players in PoP and Gates.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Give_monks_more* 
        The cost of aa's in oow... will they be like PoP or GoD in terms of pricing? 
        Most ppl consider GoD aa's overpriced, and will there be any refund of 
        GoD over priced aa's?</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> The cost 
        of AAs in Omens of War is not fixed at the moment, but I&#8217;m expecting 
        them to be priced below the level of Gates AAs. I&#8217;ll be taking a 
        look at the cost of Gates AAs in the near future and may make adjustments</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *LoD_Tsar* 
        How much of Omens content will be instanced?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> There&#8217;s 
        instanced areas for each of the appropriate level ranges that Omens is 
        set up to support for experience group content in addition to several 
        static zones. There&#8217;s also a portion of the raid content that&#8217;s 
        instanced as well, along with several static events/raids as well. Really 
        it&#8217;s a mix of both types.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Oh and Yes 
        Rashere knows Rangers have MGB. It was his attempt at humor. . . =P</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font>shoulda 
        added a smiley :(</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Bahlec* 
        what will the max ft that a char can aquire be?</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> The maximum 
        level of Flowing Thought in Omens is still under debate.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *strumsor* 
        Hello, all the bards in my guild, myself included, am curious about the 
        new charms in OOW - they seem very powerful. Could you give us a little 
        detail into what the max level for charm is? thanks</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> The final 
        level limits for charm aren&#8217;t finalized yet, so I can&#8217;t tell 
        you what they&#8217;ll be at release. They are in review and you can expect 
        to see adjustments to those spells as beta progresses.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Rykzz* 
        What can we expect in OoW for the casual gamer who might not be even close 
        to maxing out his avatar?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Casual gamers 
        will get lots of benefits from the task system. However the content is 
        also very open and available for levels 45 and up. So there will be lots 
        of room for pick up groups and xp groups without the need to raid. Only 
        one zone in the expansion even requires a flag, so head on over and enjoy 
        the new content, new items and new AAs.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Nerfme* 
        Are level 51 to 65 tasks going to be in game on release?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font>We plan to 
        at the moment but things can change. Watch for any announcements as we 
        get closer to launch.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Wemerwena-Quell* 
        based on the negative feedback in the EQforums on everquestlive, will 
        the devs change the class AA's before OOW goes live?</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> The AA 
        list on beta currently is a preliminary list that will be tweaked and 
        tuned throughout the beta testing period.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font>Again there's 
        a lot of feedback coming from prominent members of the player community 
        that are influencing our decisions as beta progresses.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> we're definitely 
        not trying to make this expansion in a vacuum</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> We also 
        need to evaluate what is best for the game and all classes and not just 
        one class. We do value your feedback on the AAs though and encourage you 
        to continue to submit it. In a constructive manner please. =)</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Guest80* 
        Will there be any high level frogs added so us frog levelers can keep 
        up the trend in OoW for getting to 70?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Well, we 
        really would like you to go out and see more of the world than just the 
        Froggie areas. Besides, you must be tired of frog legs by now.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> I play a 
        froglock! &gt;:(</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> He has warts 
        and everything</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *tearabite* 
        Will tasks be only part of OoW. Or will the tasks require you to adventure 
        all over Norrath?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> The task 
        system will involved zones all across Norrath. It should give you some 
        new reasons to visit those old zones that you might have been to in a 
        long time.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Saree3* 
        How did you choose the name &quot;Omens of war&quot;? Previous expnasions 
        carried the name of the continent mainly like Velious, Kunark &amp; Luclin.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Well we 
        wanted to sort of take a new direction with the naming conventions. We 
        think you will like the new approach. . . </p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Computer* 
        How will the new bind/gate system work. It looks like there will be new 
        secondary bind points for casters and melee will be able to gate. Is this 
        correct?</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> The Secondary 
        Recall AA will recall the player to a point that is set by use of an item 
        called a Stone of Marking. The stone has the same limitations as normal 
        bind affinity.</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> In addition, 
        a general AA will allow any class to recall back to a friendly city. That 
        point cannot be changed.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *locvz* 
        Can we expect to see more zones similar to Kod`Taz in terms of loot, tradeskills, 
        augments and experience in Omens of War? If so, will it (or they) be locked? 
        How many locked zones are planned for Omens of War?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> There will 
        be only 1 zone that requires a flag. Only 1. The rest of the zones will 
        be open and populated with all kinds of fun monsters with great items. 
      </p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Garbar* 
        Will we be able to BIND in OOW ? not being able to bind anywhere in GOD 
        is a real pain, and everyone i play with dislikes it</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> Bindable 
        locations will be less restrictive then they were in Gates. You won&#8217;t 
        be able to bind everywhere, but there will be a few locations that you 
        can bind in.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *ramsly* 
        hello :) how will the epics compare to each other in the upcomming expantion 
        and will the solo epic be worth the effort to get?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> We have 
        laid out templates for each epic quest to make sure that each is comparable. 
        No one class will have it easier than another. The epics will also be 
        just as valued from class to class for their stats. I am not sure what 
        you mean by solo epic. If you mean the epic 1.5 then yes it will be worth 
        getting and will be a natural part of the quest progression.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Naika* 
        reguarding epic 2.0....will epic 1.0 be required in order to move on to 
        2.0? thanks 8)</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> The original 
        epics are not required in order to do the quests for the new ones, but 
        possessing one of the original epics will allow you to skip some of the 
        earlier steps.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Jolo* 
        what level and class characters do the developers of Omens play?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> We all play 
        different classes and levels. I, for expample, have a high level Magician, 
        while Rytan has a Bard, Monk and Shaman. Rashere plays with his foot hair. 
      </p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font>My erudite 
        finds your halfling ways disturbing.</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> *Sylvene* 
        What are your favorite pizzas? Anyone do anchovies? :p</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> I do not 
        do anchovies. I like those new BBQ pizzas.</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> Why destroy 
        such a wonderful creation by adding little fish?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> Peperoni 
        Mushroom for the win!</p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> For our 
        final question of the night: *Sole9042* The new graphics in OoW look great! 
        Will the new graphics effect anything other than the new content added?</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Well not 
        yet is the short answer. Everything we build from now on will look that 
        good or better. We are trying to figure out how to best get all of the 
        other zones and monsters done. When we do, they would not be part of an 
        expansion and would be available to all EQ players. </p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font>(Ok I lied) 
        *naiche* will there be new visible titles before your name associated 
        with OoW AA gain?</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> We're working 
        on plans to retrofit the old world areas with the new style art as time 
        allows.</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> that was 
        for the one above BTW &gt;:)</p>
      <p><font color="#FF0000"><strong>&lt;Rashere&gt;</strong></font> Omens of 
        War introduces a new feature that allows characters to earn titles through 
        various means and then choose which one they wish to display at any time. 
      </p>
      <p><font color="#0000FF"><strong>&lt;Brannoc&gt;</strong></font> And that's 
        it for this one! Thanks for coming everyone, and be sure to join us next 
        time around. If you didn't get your question in, try next time! The logs 
        will be up on http://eq.stratics.com shortly.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Thanks all 
        for coming and chatting with us. Thanks to Stratics and their team for 
        setting this up.</p>
      <p><font color="#CC0066"><strong>&lt;Brenlo&gt;</strong></font> Special 
        thanks to Sylvene for her wit and charm =)</p>
      <p><font color="#990000"><strong>&lt;Rytan&gt;</strong></font> thanks for 
        coming everyone!<br>
      </p></td>
