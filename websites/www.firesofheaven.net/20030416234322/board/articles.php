            <td width="100%"><p><font color="#CCFFFF" face="Verdana, Arial, Helvetica, sans-serif"> 
                <br>Well, I was there from beginning to end. First I have to say it was finally nice to hear Hernan put on a proper set. He's had problems over the course of the last two years trying to play in NY. From what I've heard he's been scheduled a number of times, but seemed to keep having work visa problems and had to cancel each of his gigs here.<br> 

<p>I've listened to many of his recorded sets (both live and radio sets) and finally managed to catch him at the ultra festival at wmc this year, but I'd realized something after hearing him for 4 hours live at centro...2 hours is not enough time to fully appreciate how great hernan is, no matter how great his sets may be.</p> 

<p>This man has some great range and knowledge of tracks, good mixing skills and was able to take everyone at centro (not many unfortunately) on a nice 4 hour journey. He began in prog house territory, then shifted towards prog trance and later into trance only to quickly reverse things and bring it back towards prog house. His transitions never felt rushed and were very smooth. The trance element was very Sasha-esque (when he's on). He never took things into cheese territory imo, it was a much more sophisticated track selection, though some may say the eminem and p. diddy tracks are "cheese". All in all, it was a great night out, I'd heard many people leaving quite impressed by Hernan and I'd realized most of the people in the place were there for the GBH party moreso than for Hernan and didn't even know who the dj was when they got there, but sure as hell knew when they left.</p><br> 

<p>Some trainspots: </p><br>

John Creamer & Stephane K - Forget The World 
<br>Sander Kleinenberg - Work To Do (? Remix) 
<br>William Orbit - Water From a Vine Leaf (I think this one, all I remember was the song was Sasha's encore at Space, WMC) 
<br>Size DDD - Nobody Listens To Techno 
<br>Encore and Last Track: P. Diddy feat. Kelis - Let's Get Ill (Deep Dish Remix) <br>

<p>All in all it was an absolutely brilliant set, but I need more than a 4 hour set from Cattaneo! I could only imagine what he'd do with more time...</p>
